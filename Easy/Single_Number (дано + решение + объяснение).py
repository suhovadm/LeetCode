# Дан непустой массив целых чисел nums, в котором каждый элемент встречается дважды,
# за исключением одного. Найдите этот единственный элемент.

# Вы должны реализовать решение с линейной сложностью по времени и использовать только
# константный объем дополнительной памяти.

# Пример 1:
# Ввод: nums = [2,2,1]
# Вывод: 1

# Пример 2:
# Ввод: nums = [4,1,2,1,2]
# Вывод: 4

# Пример 3:
# Ввод: nums = [1]
# Вывод: 1

# Импортируем тип List из модуля typing.
# Это нужно для аннотации типов в определении метода.
from typing import List

# Заводим класс Solution.
class Solution:
    # Заводим метод, который принимает ряд параметров:
    # self - ссылка на экземпляр класса (обязательный первый параметр).
    # nums: List[int] - параметр метода, аннотация типа: список целых чисел.
    # -> int - аннотация возвращаемого типа: метод возвращает целое число.
    def singleNumber(self, nums: List[int]) -> int:

        # Заводим переменную, которая будет хранить промежуточный результат.
        # Это наше "аккумуляторное" значение.
        result = 0

        # Заводим цикл for, который проходит по всем элементам массива nums.
        # nums - это переменная, которая на каждой итерации принимает значение очередного элемента.
        for num in nums:

            # Операция XOR (исключающее ИЛИ) с присваиванием.
            # Это сокращенная запись: result = result ^ num.
            # XOR имеет специальные свойства, которые идеально подходят для этой задачи.
            result ^= num

        # Возвращаем финальный результат.
        # После обработки всех элементов массива, в result останется искомое уникальное число.
        return result

# 1. XOR (исключающее ИЛИ) - это битовая операция со следующими свойствами:
# a ^ 0 = a
# a ^ a = 0
# Операция коммутативна и ассоциативна: a ^ b ^ a = a ^ a ^ b = 0 ^ b = b

# 2. Как это работает в нашем случае:
# Все парные элементы при XOR друг с другом дадут 0.
# Единственный непарный элемент при XOR с 0 даст сам себя.
# Пример: [4,1,2,1,2]
# 0 ^ 4 = 4
# 4 ^ 1 = 5
# 5 ^ 2 = 7
# 7 ^ 1 = 6
# 6 ^ 2 = 4 <- результат.

# 3. Сложность:
# Время: O(n) - один проход по массиву.
# Память: O(1) - используем только одну переменную result.
# Это самый оптимальный способ решения данной задачи, полностью соответствующий требованиям.
