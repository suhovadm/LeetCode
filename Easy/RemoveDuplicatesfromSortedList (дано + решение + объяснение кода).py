# Дан отсортированный список. Удалите все дубликаты так, чтобы каждый элемент
# встречался только один раз. Верните связанный список также в отсортированном виде.

# Пример 1:
# Input = [1,1,2]
# Output = [1,2]

# Пример 2:
# Input = [1,1,2,3,3]
# Output = [1,2,3]

# Импортируем Optional из модуля typing для указания,
# что параметры/результаты могут быть либо ListNode, либо None.
from typing import Optional

# Заводим класс ListNode для узла связанного списка.
# Каждый узел имеет:
# val - значение узла (по умолчанию 0).
# next - ссылка на следующий узел (по умолчанию None - конец списка).
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next

# Определяем основной класс Solution с методом deleteDuplicates.
class Solution:

    # Метод принимает head - начало связанного списка (может быть None для пустого списка).
    # Возвращает тоже ListNode или None).
    def deleteDuplicates(self, head: Optional[ListNode]) -> Optional[ListNode]:

        # Создаём переменную current, которая указывает на текущий узел.
        # Начинаем с головы списка.
        current = head

        # Запускаем бесконечный цикл, который будет работать пока:
        # current не равен none (не дошли до конца списка)
        # current.next не none (есть следующий узел для сравнения)
        while current and current.next:
            if current.val == current.next.val:
                current.next = current.next.next

            # Если значения текущего и следующего узла равны (дубликат):
            # пропускаем следующий узел, перенаправляем ссылку current.next через один узел.
            # Пример: было A -> B -> C, где A.val == B.val, стало A -> C.
            # ВАЖНО: current не меняем, так как нужно проверить, есть ли ещё дубликаты.

            # Если значения разные:
            # переходим к следующему узлу в списке.
            else:
                current = current.next

        # Возвращаем исходную голову списка (она не меняется).
        return head

# Тестирование.

# Пример 1.
# Создаём связанный список 1 -> 1 -> 2 -> None.
head1 = ListNode(1, ListNode(1, ListNode(2))) # Input [1,1,2]

# Вызываем нашу функцию для удаления дубликатов.
result1 = Solution().deleteDuplicates(head1)

# Преобразуем связанный список обратно в обычный список для вывода.
# Проходим по всем узлам, собирая значения в список output1.
output1 = []
while result1:
    output1.append(result1.val)
    result1 = result1.next

# Выводим результат.
print('Пример 1: ', output1) # Output [1,2]

# Пример 2.
# Создаём 1 -> 1 -> 2 -> 3 -> 3 -> None.
head2 = ListNode(1, ListNode(1, ListNode(2, ListNode(3, ListNode(3))))) # Input [1,1,2,3,3]

# Вызываем нашу функцию для удаления дубликатов.
result2 = Solution().deleteDuplicates(head2)

# Преобразуем связанный список обратно в обычный список для вывода.
# Проходим по всем узлам, собирая значения в список output2.
output2 = []
while result2:
    output2.append(result2.val)
    result2 = result2.next

# Выводим результат.
print('Пример 2: ', output2) # Output [1, 2, 3]

# 1. Список уже отсортирован - это важно, дубликаты всегда рядом.
# 2. Изменяем только ссылки, не создаём новых узлов.
# 3. Сложность O(n) - проходим по списку один раз.
# 4. Память O(1) - не используем дополнительную память, только переменные.
# 5. Не меняем голову списка - head остается тем же узлом.

# Алгоритм эффективен именно благодаря отсортированности списка - дубликаты гарантированно
# находятся рядом, что позволяет решить задачу за один проход.
