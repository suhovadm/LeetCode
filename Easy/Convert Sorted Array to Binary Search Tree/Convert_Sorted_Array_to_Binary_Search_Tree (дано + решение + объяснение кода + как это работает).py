# Convert Sorted Array to Binary Search Tree (Easy).

# Дан целочисленный массив nums, элементы которого отсортированы по возрастанию.
# Необходимо преобразовать его в сбалансированное по высоте бинарное дерево поиска (BST).

# Пример 1:
# вход: nums = [-10, -3, 0, 5, 9]
# выход: [0, -3, 9, -10, null, -5] * опечатка в исходном условии задачи! -5 нет на входе!!!
# пояснение:
# также допустим вариант:
# [0, -10, 5, null, -3, null, 9]

# Пример 2:
# вход: nums = [1, 3]
# выход: [3, 1]
# пояснение:
# допустимы оба варианта:
# [1, null, 3] и [3, 1] - оба являются сбалансированными BST.

# Ограничения:
# 1 <= nums.length <= 10⁴
# -10⁴ <= nums[i] <= 10⁴
# nums отсортирован по строго возрастающему порядку.

# То есть, наша задача, из отсортированного массива построить сбалансированное бинарное
# дерево поиска (BST). Главная цель сделать так, чтобы дерево было максимально "ровным",
# то есть: глубина слева = глубине справа, а операции поиска работали быстро.

# Пример:
# nums = [-10, -3, 0, 5, 9] * Первый вариант, в выводе будет второй вариант. Оба варианта верные!
# mid = (left + right) // 2 - это где -10 сверху!
# mid = (left + right + 1) // 2 - это где -3 сверху!
#
#         0        ← середина
#        / \
#     -10   5
#       \     \
#       -3     9

# --------------------------------------------------------------------------------------

# Импортируем List и Optional из модуля typing.
# List нужен для аннотации типа "список".
# Optional означает, что значение может быть либо указанного типа, либо None.
from typing import List, Optional

# Заводим класс TreeNode для представления узла бинарного дерева.
class TreeNode:
    # Конструктор принимает 3 параметра со значениями по умолчанию.
    # val - значение узла (по умолчанию 0).
    # left - левый дочерний узел (по умолчанию None).
    # right - правый дочерний узел (по умолчанию None).
    def __init__(self, val=0, left=None, right=None):

    # Сохраняем переданные значения в атрибуты экземпляра.
        self.val = val # значение узла,
        self.left = left # ссылка на левого потомка,
        self.right = right # ссылка на правого потомка.

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Заводим основной метод sortedArrayToBST, который принимает ряд параметров.
    # nums - отсортированный список целых чисел.
    # -> возвращаем либо объект TreeNode, либо None (если список пуст).
    def sortedArrayToBST(self, nums: List[int]) -> Optional[TreeNode]:

    # Заводим внутренний метод build.
    # left и right - индексы границ текущего подмассива (включительно).
        def build(left, right):
    # Базовый случай рекурсии: если левая граница больше правой,
    # это говорит нам о том, что подмассив пуст (нет элементов для обработки).
            if left > right:
                return None

    # Находим индекс среднего элемента в текущем диапазоне.
    # // - целочисленное деление, округление вниз.
    # Для чётной длины массива выбираем правый средний элемент (варивант Б).
            mid = (left + right + 1) // 2

    # Создаём новый узел дерева.
    # Значением узла будет элемент массива с индексом mid.
            node = TreeNode(nums[mid])

    # Рекурсивный вызов для левого поддерева.
    # Передаём диапазон от left до mid - 1.
    # Все элементы левее середины пойдут в левое поддерево.
            node.left = build(left, mid - 1)

    # Рекурсивный вызов для правого поддерева.
    # Передаём диапазон от mid + 1 до right.
    # Все элементы правее середины пойдут в правое поддерево.
            node.right = build(mid + 1, right)

    # Возвращаем созданный узел со всеми его потомками.
            return node

    # Запускаем рекурсивное построение дерева.
    # Начальный диапазон: от первого индекса (0) до последнего ( len(nums) - 1 ).
        return build(0, len(nums) - 1)

# Как это работает ?
# Берём середину массива - это корень.
# Левую часть строим как левое поддерево.
# Правую часть - как правое поддерево.
# Повторяем рекурсивно.