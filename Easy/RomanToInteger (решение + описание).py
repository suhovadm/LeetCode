# Дано: строка с римскими цифрами, например MCMXCIV.
# Что нужно сделать? Надо превратить эти римские цифры в обычное число, например, 1994.
# Как это сделать? У каждой римской буквы есть своё значение:
# I = 1, V = 5, X = 10 и т.д.
# Обычно просто складываем значения слева направо: III = 1+1+1 = 3.
# НО! Есть особые случаи, когда меньшая цифра стоит перед большей - тогда нужно вычитать,
# а не складывать:
# IV = 4 (5-1)
# IX = 9 (10-1)
# CM = 900 (1000-100)
# Как определить, когда вычитать?
# Смотрим на две соседние буквы.
# Если левая буква меньше правой -> вычитаем левую.
# Если левая буква больше или равна правой -> прибавляем левую.
# Пример MCMXCIV = 1994
# 1. M (1000) -> +1000
# 2. C (100) -> стоит перед M(1000), и 100 < 1000 -> вычитаем 100
# 3. M (1000) -> +1000
# 4. X (10) -> стоит перед C (100), и 10 < 100 -> вычитаем 10
# 5. C (100) -> +100
# 6. I (1) -> стоит перед V (5), и 1 < 5 -> вычитаем 1
# 7. V (5) -> +5
# Всё вместе: 1000 - 100 + 1000 - 10 + 100 - 1 + 5 = 1994.

# Заводим класс Solution, как того требует LeetCode.
class Solution:

    # Заводим метод romanToInt, который принимает строку s и возвращает целое число.
    def romanToInt(self, s: str) -> int:

        # Заводим словарь, где ключ - римская цифра, а значение - это число.
        values = {
            'I': 1,
            'V': 5,
            'X': 10,
            'L': 50,
            'C': 100,
            'D': 500,
            'M': 1000
        }

        # Заводим переменную total для хранения итоговой суммы.
        total = 0

        # Получаем длину строки с римским числом.
        n = len(s)

        # Заводим цикл по всем символам строки от первого до последнего.
        for i in range(n):

            # Получаем числовое значение текущего символа из словаря.
            current_value = values[s[i]]

            # Проверяем два условия.
            # - Есть ли следующий символ (i + 1 < n).
            # - Больше ли значение следующего символа, чем текущего.
            if i + 1 < n and values[s[i + 1]] > current_value:

                # Если да, то это случай вычитания (IV, IX и т.д.)
                # Вычитаем значение текущего символа.
                total -= current_value
            else:

                # Если нет, то это обычный случай сложения.
                # Прибавляем значение текущего символа.
                total += current_value

        # Возвращаем итоговую сумму.
        return total

# Создаём объект класса Solution.
solution = Solution()

# Пример.
print(solution.romanToInt('III')) # 3 (1+1+1 = 3)
print(solution.romanToInt('LVIII')) # 58 (50 + 5 + 1 + 1 + 1 = 58)
print(solution.romanToInt('MCMXCIV')) # 1994 (1000 - 100 + 1000 - 10 + 100 - 1 + 5 = 1994)
