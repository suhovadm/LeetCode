# Импортируем Optional из модуля typing.
# Optional - специальный тип, означающий, что значение может быть либо указанного типа,
# либо None.
# Здесь Optional[ListNode] означает, что аргумент head может быть либо объектом класса
# ListNode, либо None.
from typing import Optional

# Заводим класс ListNode.
# Класс описывает узел односвязного списка.
class ListNode:

    # __init__(self, x) - это конструктор, который вызывается при создании узла.
    # Он принимает x-значение, которое будет хранится в узле.
    def __init__(self, x):
        # self.val = x - сохраняем переданное значение в атрибут val экземпляра класса.
        self.val = x
        # self.next = None - изначально узел ни на что не указывает (следующего узла нет).
        # В процессе построения списка этот атрибут будет ссылаться на следующий узел или None (конец списка).
        self.next = None

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод hasCycle, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. В данном случае, дефолтный параметр.
    # head с аннотацией типа Optional[ListNode].
    # -> bool - возврат булева значения типа True, или False.
    # Задача метода: определить, есть ли в связном списке цикл, т.е. не заканчивается ли
    # список на None, а замыкается на один из предыдущих узлов.
    def hasCycle(self, head: Optional[ListNode]) -> bool:

    # Заводим два указателя.
    # Создаём две переменные - slow и fast, обе ссылаются на тот же узел,
    # что и head (начало списка).
    # slow - это "черепаха", она будет двигаться на один шаг за итерацию.
    # fast - это "заяц", он будет двигаться на два шага за итерацию.
    # Такой приём используется в алгоритме Флойда для поиска цикла.
        slow = fast = head

    # Заводим цикл while.
    # Цикл продолжается до тех пор, пока fast указывает на реальный узел (не None)
    # и у этого узла есть следующий ( fast.next, не None ).
    # Зачем проверять fast.next ? Потому что внутри цикла мы делаем fast = fast.next.next.
    # Если fast.next был бы None, то fast.next.next вызвало бы ошибку (атрибут next у None
    # отсутствует).
    # Условие гарантирует, что fast может сделать два шага, не выходя за пределы списка.
    # Если список пуст ( head is None ) или состоит из одного узла (без цикла), то условие
    # сразу станет ложным ( fast is None или fast.next is None ), цикл не выполнится,
    # метод вернёт False.
        while fast and fast.next:

    # Тело цикла - перемещение указателей.
    # slow = slow.next - черепаха сдвигается на один узел вперёд.
    # fast = fast.next.next - заяц сдвигается сразу на два узла вперёд, т.е.
    # перепрыгивает через один узел.
    # Важно: после этого шага slow и fast могут указывать на один и тот же узел
    # (если есть цикл) или на разные.
            slow = slow.next
            fast = fast.next.next

    # Проверка встречи указателей.
    # Если slow и fast указывают на один и тот же объект узла, значит они встретились.
    # В списке без цикла fast всегда доберётся до конца ( None ) и цикл завершится, встреча
    # невозможна.
    # В списке с циклом заяц (который быстрее) рано или поздно догонит черепаху внутри цикла.
    # Поэтому их равенство - признак наличия цикла.
    # Как только встреча произошла, метод немедленно возвращает True.
            if slow == fast:
                return True

    # Завершение цикла и возврат False.
    # Если цикл while завершился (т.е. условие fast and fast.next стало ложным),
    # значит fast достиг конца списка ( None или у последнего узла next = None ).
    # Это доказывает, что список конечен и не содержит цикла.
    # Метод возвращает False.
        return False