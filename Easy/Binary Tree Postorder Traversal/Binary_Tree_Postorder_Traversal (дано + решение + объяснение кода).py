# Мы хотим просто обойти всё дерево и записать значения узлов в правильном порядке.
# Сначала левый потомок - потом правый - потом сам узел.
# Сам узел (корень) идёт самым последним, т.е. 1 идёт самой последней.
# 3 - 2 - 1, т.е. [3, 2, 1]

