# Заводим класс Solution.
class Solution:
    # Заводим метод climbStairs, который принимает целое число n и возвращает целое число.
    def climbStairs(self, n: int) -> int:

        # Если всего 1 ступенька - только 1 способ подняться (1 шаг).
        if n == 1:
            return 1

        # Если 2 ступеньки - 2 способа (1 + 1 или 2 шага).
        if n == 2:
            return 2

        # Создаём список из (n + 1) нулей.
        # Индекс в списке соответствует количеству ступенек.
        # ways[i] будет хранить количество способов добраться до i-й ступеньки.
        ways = [0] * (n + 1)

        # 1 способ для 1 ступеньки.
        ways[1] = 1
        # 2 способа для 2 ступенек.
        ways[2] = 2

        # Начиная с 3-й ступеньки, вычисляем количество способов:
        # способы добраться до i-й ступеньки =
        # способы добраться до (i-1)-й ступеньки +
        # способы добраться до (i-2)-й ступеньки
        for i in range(3, n + 1):
            ways[i] = ways[i - 1] + ways[i - 2]

        # Возвращаем количество способов для n ступенек.
        return ways[n]
