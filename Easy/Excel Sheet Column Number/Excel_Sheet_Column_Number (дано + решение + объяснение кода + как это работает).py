# Excel Sheet Column Number

# Дана строка columnTitle, которая представляет название столбца в таблице Excel.
# Необходимо вернуть соответствующий номер этого столбца.

# Примеры:
# A - 1
# B - 2
# C - 3
# ...
# Z - 26
# AA - 27
# AB - 28
# ...

# Пример 1:
# Ввод: columnTitle = "A"
# Вывод: 1

# Пример 2:
# Ввод: columnTitle = "AB"
# Вывод: 28

# Пример 3:
# Ввод: columnTitle = "ZY"
# Вывод: 701

# -----------------------------------------------------------------------------------

# Заводим класс, как того требует LeetCode.
class Solution:
    # Объявляем метод titleToNumber, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. В нашем случае просто дефолтный параметр.
    # columnTitle: str - входная строка, например "AB".
    # -> int - функция, которая возвращает целое число.
    def titleToNumber(self, columnTitle: str) -> int:

    # Переменная result, которая будет накапливать итоговое число.
        result = 0

    # Проходимся циклом по каждому символу строки.
    # Например, если columnTitle = "AB", то сначала char = 'A', затем char = 'B'.
        for char in columnTitle:

    # Основная формула.

    # a) ord(char) - код символа в Unicode/ASCII
    # ord('A') = 65
    # ord('B') = 66
    # ord('Z') = 90

    # б) ord(char) - ord('A')
    # Переводим букву в число от 0 до 25:
    # 'A' - 65 - 65 = 0
    # 'B' - 66 - 65 = 1
    # 'Z' - 90 - 65 = 25

    # в) +1
    # Так как в Excel A = 1, а не 0:
    # 'A' - 0 + 1 = 1
    # 'B' - 1 + 1 = 2
    # 'Z' - 25 + 1 = 26

    # Таким образом, выражением (ord(char) - ord('A') + 1) даёт цифру для текущего
    # разряда (от 1 до 26).

    # г) result * 26
    # Сдвиг уже накопленного значения влево на один 26-ричный разряд.
    # Это как в десятичной системе: если у нас есть число X и мы добавляем новую цифру Y,
    # то новое число = X * 10 + Y. Здесь основание 26.
            result = result * 26 + (ord(char) - ord('A') + 1)

    # Возвращаем результат.
        return result

# -----------------------------------------------------------------------------------

# Как это работает?

# Идём слева направо.
# Каждый раз:
#   - умножаем текущий результат на 26,
#   - прибавляем значение текущей буквы,
#   - ord(char) - ord('A') + 1 переводит букву в число (A=1, ... , Z=26).

# Что такое ord() - это встроенная функция в Python, которая возвращает числовой код символа.
# Проще говоря: она переводит символ в число по таблице Unicode.

# Пример:
# ord('A') # 65
# ord('B') # 66
# ord('a') # 97