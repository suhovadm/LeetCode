# Remove Linked List Elements (Easy).

# Дано: голова связного списка и целое число val. Нужно удалить все узлы связного списка,
# у которых Node.val == val, и вернуть новую голову списка.

# Пример 1:
# вход: head = [1, 2, 6, 3, 4, 5, 6], val = 6
# выход: [1, 2, 3, 4, 5]

# Пример 2:
# вход: head = [], val = 1
# выход: []

# Пример 3:
# вход: head = [7, 7, 7, 7], val = 7
# выход: []

# Ограничения:
# количество узлов в списке находится в диапазоне [0, 10⁴]
# 1 ≤ Node.val ≤ 50
# 0 ≤ val ≤ 50

# Чего мы, собственно, пытаемся добиться ?
# Мы проходимся по списку и удаляем все узлы, у которых значение равно val, не создавая
# при этом новый список, а просто "перепрыгивая" через ненужные элементы с помощью ссылок.
# То есть, наша задача: сохранить порядок списка, но выкинуть все узлы с нужным значением.

# ------------------------------------------------------------------------------------------

# Импортируем тип Optional из модуля typing.
# Optional[ListNode] означает, что значение может быть либо ListNode, либо None.
from typing import Optional

# Заводим класс ListNode. Это класс, который описывает узел списка.
class ListNode:
    def __init__(self, val=0, next=None):

        # val - значение узла (по умолчанию = 0).
        # next - ссылка на следующий узел (по умолчанию = None).
        self.val = val
        self.next = next

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод removeElements, который принимает в себя ряд параметров.
    # head - первый узел списка (может быть None, если список пуст).
    # val - значение, которое нужно удалить.
    # -> возвращает новый head (тоже Optional[ListNode]
    def removeElements(self, head: Optional[ListNode], val: int) -> Optional[ListNode]:

    # Создаём фиктивный узел (dummy) со значением 0.
    # Он не является частью исходных данных, его задача - упростить удаление первого элемента.
    # Зачем это нужно?
    # Если нужно удалять первый узел, без dummy пришлось бы обрабатывать этот случай отдельно.
    # С dummy - логика становится единообразной для всех узлов.
        dummy = ListNode(0)
    # Связываем фиктивный узел с реальным началом списка.
    # Теперь список выглядит так: [dummy] -> [head] -> ...
        dummy.next = head

    # prev - это указатель на предыдущий узел относительно текущего.
    # Изначально указывает на dummy.
        prev = dummy
    # curr - указатель на текущий узел, который мы проверяем.
    # Начинаем с первого реального узла.
        curr = head

    # Запускаем цикл while.
    # Цикл идёт, пока curr не станет None (то есть пока не достигнут конец списка).
        while curr:
    # Проверяем: равно ли значение текущего узла заданному val.
            if curr.val == val:
    # Удаление узла:
    # перекидываем ссылку предыдущего узла на следующий после curr.
    # Узел curr исключается из списка (сборщик мусора удалит его позже).
    # Было: prev -> curr -> next, стало: prev -> next.
                prev.next = curr.next
    # Если значение не равно val, значит, узел остаётся в списке.
            else:
    # Передвигаем prev на текущий узел, потому что при следующей итерации
    # curr уйдёт вперёд, а prev должен указывать на предыдущий относительно нового curr.
                prev = curr
    # Передвигаем curr на следующий узел.
    # ! Это выполняется всегда, независимо от того, был ли удалён узел или нет.
            curr = curr.next

    # Возврат результата.
    # Возвращаем следующий за dummy узел - это и есть новый head списка после всех удалений.
    # Если все узлы были удалены, то dummy.next будет None.
        return dummy.next

