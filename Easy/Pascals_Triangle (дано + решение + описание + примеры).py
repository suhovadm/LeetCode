# Дано целое число numRows, верните первые numRows, строк треугольника Паскаля.

# В треугольнике Паскаля каждое число является суммой двух чисел, находящихся
# непосредственно над ним, как показано:

# Пример 1:
# Ввод: numRows = 5
# Вывод: [ [1], [1,1], [1,2,1], [1,3,3,1], [1,4,6,1] ]

# Пример 2:
# Ввод: numRows = 1
# Вывод: [ [1] ]

# Импортируем тип List из модуля typing для аннотации типов.
from typing import List

# Заводим класс Solution.
class Solution:

    # Заводим метод generate, который принимает следующие параметры:
    # self - ссылка на экземпляр класса.
    # numsRows: int - количество строк треугольника Паскаля.
    # -> List[List[int]] - метод, который возвращает список списков целых чисел.
    def generate(self, numRows: int) -> List[List[int]]:

        # Создаём пустой треугольник, который будет хранить весь треугольник Паскаля.
        triangle = []

        # Запускаем цикл, который выполнится numRows раз.
        # Каждая итерация создаёт одну строку треугольника.
        for i in range(numRows):

            # Создаём новую строку для треугольника.
            # i + 1 - определяет длину строки (строка 0 имеет длину 1, строка 1 имеет длину 2 и т.д.)
            # [1] * (i + 1) - создаёт список из (i + 1) едениц.
            # Пример: при i = 3 -> row = [1, 1, 1, 1] (пока все элементы = 1).
            row = [1] * (i + 1)

            # Вложенный цикл для обновления внутренних элементов строки.
            # j принимает значения от 1 до i-1.
            # Этот цикл выполняется только когда i >= 2 (так как range(1, i) пуст при i = 0
            # или i = 1).
            # Цель: обновить элементы между первым и последним (которые уже = 1).
            for j in range(1, i):

                # Берём предыдущую строку из треугольника.
                # triangle[i-1] - строка с индексом i-1 (предыдущая относительно текущей).
                # Пример: при i = 3, берём строку triangle[2] (третью строку).
                prev_row = triangle[i-1]

                # Вычисляем значение текущего элемента по правилу треугольника Паскаля:
                # prev_row[j-1] - элемент слева сверху.
                # prev_row[j] - элемент справа сверху.
                # Пример: для row[1] при i = 2: row[1] = triangle[1][0] + triangle[1][1] = 1 + 1 = 2
                row[j] = prev_row[j-1] + prev_row[j]

            # Добавляем готовую строку row в треугольник triangle.
            # Теперь triangle содержит на одну строку больше.
            triangle.append(row)

        # Возвращаем готовый треугольник Паскаля, содержащий numRows строк.
        return triangle

# Запускаем программу, делаем вывод в консоль.
if __name__ == "__main__":
    solution = Solution()

    print("numRows = 5:")
    result1 = solution.generate(5)
    for row in result1:
        print(row)

    print('\nnumRows = 1:')
    result2 = solution.generate(1)
    for row in result2:
        print(row)

    print('\nnumRows = 0:')
    result3 = solution.generate(0)
    print(result3)
