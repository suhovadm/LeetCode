# Complement of Base 10 Integer

# Дополнение целого числа - это целое число, которое получается путём замены всех 0 на 1
# и всех 1 на 0 в его двоичном представлении.

# Например, целое число 5 в двоичной системе представляется как "101", а его дополнение - "010",
# что соответствует целому числу 2.
# Для заданного числа n верните его дополнение.

# Пример 1:
# Вход: n = 5
# Выход: 2
# Пояснение: 5 в двоичной системе - это "101", а его дополнение в двоичной системе - "010",
# что в десятичной системе равно 2.

# Пример 2:
# Вход: n = 7
# Выход: 0
# Пояснение: 7 в двоичной системе - это "111", а его дополнение в двоичной системе - "000",
# что в десятичной системе равно 0.

# Пример 3:
# Вход: n = 10
# Выход: 5
# Пояснение: 10 в десятичной системе - это "1010", а его дополнение в двоичной системе - "0101",
# что в десятичной системе равно 5.

class Solution:
    def bitwiseComplement(self, n: int) -> int:

        if n == 0:
            return 1

        mask = 1
        while mask < n:
            mask = (mask << 1) | 1

        return n ^ mask

# Как это работает?

# 1. Для числа 0 обрабатываем отдельно, так как его дополнение должно быть 1.

# 2. Создаём маску, которая имеет еденицы во всех позициях, где у числа n есть значащие биты:
# - начинаем с mask = 1,
# - пока mask < n, сдвигаем mask влево и добавляем 1 в младший бит.
# Например, для n = 5 (101 в двоичной):
# mask = 1 (1)
# mask = (1 << 1) | 1 = 3 (11)
# mask = (3 << 1) | 1 = 7 (111)
# Маска 7 (111) имеет столько же бит, сколько и число 5 (101).

# 3. Операция XOR (^) с маской инвертирует только те биты, которые есть в исходном числе:
# - 101 ^ 111 = 010 (двоичная), что равно 2.

# Временная сложность: O(log n) - проходим по битам числа.
# Пространственная сложность: O(1) - используем только несколько переменных.