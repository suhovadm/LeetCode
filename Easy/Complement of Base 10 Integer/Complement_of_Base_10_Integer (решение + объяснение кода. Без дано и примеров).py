class Solution:
    # Определеям метод bitwiseComplement, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса.
    # n: int - целое число.
    # -> int - возвращает целое число (его дополнение).
    def bitwiseComplement(self, n: int) -> int:

    # Обрабатываем особый случай - число 0.
    # Если n = 0, его двоичное представление - "0".
    # Дополнение должно быть "1", что в десятичной системе равно 1.
    # Возвращаем 1 сразу, не выполняя дальнейший код.
        if n == 0:
            return 1

    # Создаём переменную mask со значением 1.
    # В двоичном виде: 1 - это один бит.
        mask = 1

    # Запускаем цикл, который будет выполняться, пока маска меньше исходного числа n.
    # Нам нужно создать маску, которая по длине (количеству бит) равна числу n.
    # Например, для n = 5 (двоичное 101 - 3 бита) маска должна стать 111 (тоже 3 бита).
        while mask < n:
    # Увеличиваем длину маски на 1 бит.
    # mask << 1 - сдвигаем все биты маски влево на 1 позицию (добавляем 0 справа).
    # | 1 - устанавливаем самый правый (младший) бит в 1 с помощью операции OR.
    # В результате каждый раз добавляем справа еденицу, и маска становится последовательностью
    # из всех едениц возрастающей длины.
    # Например для n = 5 (101):
    # итерация 1: mask = 1 (1 бит),
    # проверка: 1 < 5 - true,
    # mask = (1 << 1) | 1 = 2 | 1 = 3 (в двоичной системе 11 - 2 бита).
    # Итерация 2: mask = 3 (2 бита),
    # проверка: 3 < 5 - true,
    # mask = (3 << 1) | 1 = 6 | 1 = 7 (в двоичной системе 111 - 3 бита).
    # Итерация 3: mask = 7 (3 бита),
    # проверка: 7 < 5 - false - цикл завершён.
            mask = (mask << 1) | 1

    # Возвращаем результат операции XOR (исключающее ИЛИ) между исходным числом и маской.
    # XOR работает так: 0 ^ 1 = 1, 1 ^ 1 = 0
    # Это идеально подходит для инвертирования битов:
    # - там, где в маске стоит 1, бит числа инвертируется,
    # - там, где в маске стоит 0, бит числа остаётся без изменений.
    # Поскольку маска состоит из всех едениц в значащих битах числа n, все биты
    # числа инвертируются.
    # Пример для n = 5:
    # n = 101 (двоичная),
    # mask = 111 (двоичная),
    # 101 ^ 111 = 010 (двоичная) = 2 (десятичная).
        return n ^ mask