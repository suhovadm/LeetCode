# Valid Palindrome.

# Фраза является палиндромом, если после преобразования всех заглавных букв в строчные
# и удаления всех неалфавитно-цифровых символов она читается одинаково слева направо и справа
# налево. Алфавитно-цифровые символы включают буквы и цифры.

# Дана строка s, верните true, если она является палиндромом, или false в противном случае.

# Пример 1:
# вход: s = "A man, a plan, a canal: Panama"
# выход: true
# Объяснение: "amanaplanacanalpanama" - это палиндром.

# Пример 2:
# вход: s = "race a car"
# выход: false
# объяснение: "raceacar" не является палиндромом.

# Пример 3:
# вход: s = " "
# выход: true
# объяснение: после удаления неалфавитно-цифровых символов строка s становится пустой строкой "".
# Поскольку пустая строка читается одинаково слева направо и справа налево, она является
# палиндромом.

# Максимально простой и эффективный скрипт с использованием двух указателей.

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Объявляем метод isPalindrome, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. В нашем случае ничего не делает, просто дефолтный параметр.
    # s: str - принимает строку.
    # -> bool - возвращает булево значение, т.е. true, или false.
    def isPalindrome(self, s: str) -> bool:

    # Заводим 2 указателя.
    # left - указывает на первый символ строки (индекс 0).
    # right - указывает на последний символ строки (индекс len(s) - 1).
        left = 0
        right = len(s) - 1

    # Начинаем цикл, который будет выполняться, пока левый указатель находится левее правого.
    # Когда они встретятся или пересекутся, это будет значить, что мы проверили все символы.
        while left < right:

    # Пропускаем все неалфавитно-цифровые символы слева:
    # пока left меньше right и символ по индексу left НЕ является буквой или цифрой
    # двигаем левый указатель вправо ( left += 1 ).
    # В результате left остановится на первом валидном символе или сравняется с right.
            while left < right and not s[left].isalnum():
                left += 1

    # Пропускаем все неалфавитно-цифровые символы справа:
    # пока left меньше right и символ по индексу right НЕ является буквой или цифрой
    # двигаем правый указатель влево ( right -= 1 ).
    # В результате right остановится на первом валидном символе справа или сравняется с left.
            while left < right and not s[right].isalnum():
                right -= 1

    # Приводим оба символа к нижнему регистру с помощью .lower()
    # Если они не равны, значит строка НЕ является палиндромом.
    # Возвращаем false и завершаем выполнение функции.
            if s[left].lower() != s[right].lower():
                return False

    # Двигаем указатели к центру.
    # Увеличиваем левый указатель на 1.
    # Увеличиваем правый указатель на 1.
            left += 1
            right -= 1

    # Если цикл завершился (все пары символов совпали), значит строка является
    # палиндромом. Возвращаем True.
        return True

# Как это работает?

# 1. Устанавливаем два указателя: в начале и в конце строки.

# 2. Пока указатели не встретились:
#   - пропускаем все небуквенно-цифровые символы слева
#   - пропускаем все небуквенно-цифровые символы справа
#   - сравниваем символы
#   - если не совпадают - возвращаем False
#   - двигаем оба указателя к центру

# 3. Если все проверки пройдены - возвращаем True.