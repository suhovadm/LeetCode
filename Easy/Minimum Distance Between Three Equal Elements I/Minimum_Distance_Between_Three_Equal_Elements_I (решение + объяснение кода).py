# Импортируем типы данных из модуля typing:
# List - для аннотации типа списка,
# Dict - для аннотации типа словаря,
# Tuple - для аннотации типа кортежа.
from typing import List, Dict, Tuple

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод minimumDistance, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. Дефолтный параметр для методов класса.
    # nums: List[int] - входной параметр: список целых чисел.
    # -> int - метод возвращает целое число.
    def minimumDistance(self, nums: List[int]) -> int:

    # Заводим переменные.
    # Создаём словарь last, где:
    # ключ - это число из списка nums, а
    # значение - это кортеж из двух целых чисел (i1 и i2), где
    # i1 - индекс предыдущего вхождения этого числа (или -1, если это первое вхождение)
    # i2 - индекс последнего вхождения этого числа.
        last: Dict[int, Tuple[int, int]] = {}

    # Заводим ответ в качестве бесконечности.
    # float('inf') - специальное значение "положительная бесконечность".
    # Позже мы будем искать минимальное расстояние, поэтому начинаем с максимально
    # возможного значения.
        ans = float('inf')

    # Основной цикл.
    # Проходимся по списку nums с получением одновременно:
    # i - индекса текущего элемента,
    # x - значения текущего элемента.
        for i, x in enumerate(nums):

    # Обработка текущего числа.
    # Проверяем, встречалось ли число x раньше (есть ли оно в словаре last).
            if x in last:

                # Если число уже встречалось:
                # распаковываем кортеж, связанный с ключом x:
                # i1 - индекс предыдущего вхождения (или -1),
                # i2 - индекс последнего вхождения.
                i1, i2 = last[x]

                # Проверяем, есть ли у нас предыдущее вхождение (не первое ли это
                # вхождение после инициализации).
                if i1 != -1:

                    # Если у нас есть три вхождения числа:
                    # i1 — предпредыдущее, i2 — предыдущее, i — текущее,
                    # то они образуют тройку (i1, i2, i).
                    # Расстояние:
                    # |i1 - i2| + |i2 - i| + |i - i1| = 2 * (i - i1)
                    # Поэтому достаточно учитывать только первый и третий индекс.
                    ans = min(ans, 2 * (i - i1))

                # Обновляем запись: последним вхождением становится текущее, а
                # предыдущее - это то, что было последним до этого.
                last[x] = (i2, i)

            # Если число встречается впервые:
            # сохраняем кортеж (-1, i) - предыдущего вхождения нет (-1), последнее
            # вхождение - текущий индекс i.
            else:
                last[x] = (-1, i)

        # Возврат результата.
        # Здесь идём через тернарный оператор if / else.
        # Если нашли хотя бы одну подходящую тройку, возвращаем ans.
        # Иначе возвращаем -1 (пары не найдены).
        return ans if ans != float('inf') else -1

