# Excel Sheet Column Title

# Дано целое число columnNumber (номер столбца).
# Верните соответствующее ему название столбца, как в таблице Excel.

# Например:
# A - 1
# B - 2
# C - 3

# Z - 26
# AA - 27
# AB - 28

# Пример 1:
# вход: columnNumber = 1
# выход: "A"

# Пример 2:
# вход: columnNumber = 28
# выход: "AB"

# Пример 3:
# выход: columnNumber = 701
# выход: "ZY"

# Ограничения:
# 1 ≤ columnNumber ≤ 2³¹ − 1

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод convertToTitle, который принимает целое число columnNumber
    # и возвращает строку (str).
    def convertToTitle(self, columnNumber: int) -> str:

    # Создаём пустой список result, в который будем собирать буквы в обратном порядке.
    # От младших разрядов к старшим.
        result = []

    # Заводим бесконечный цикл while.
    # Цикл продолжается, пока номер колонки больше нуля.
    # В конце каждой итерации мы будем уменьшать columnNumber в 26 раз, пока не дойдём до нуля.
        while columnNumber > 0:
    # Почему вычитаем 1? Потому что наша система счисления начинается с 1 (A=1), а не с 0.
    # Пример: для числа 26 (Z) после вычитания 1 получаем 25, и 25 % 26 = 25 -> Z.
    # Если бы не вычитали, то 26 % 26 = 0, что не соответствует ни одной букве.
            columnNumber -= 1
    # columnNumber % 26 - получаем остаток от деления на 26, который будет от 0 до 25.
    # ord('A') - получаем ASCII код буквы A (65).
    # Складываем: остаток + 65 - получаем ASCII код нужной буквы.
    # chr() преобразует ASCII код в символ.
            result.append(chr(columnNumber % 26 + ord('A')))
    # Целочисленное деление на 26 (без остатка).
    # Переходим к следующему разряду.
    # Пример для числа 28 (AB):
    # - итерация 1: 28 - 27 - 27%26=1 - B, затем 28//26=1
    # - итерация 2: 1 - 0 - 0%26=0 - A, затем 0//26=0, цикл завершается.
            columnNumber //= 26

    # Возврат результата.
    # reversed(result) - разворачиваем список, потому что мы собирали буквы от младшего разряда к старшему.
    # ''join(...) - объединяем список букв в одну строку без разделений.
        return ''.join(reversed(result))

# Пример работы:
# Для columnNumber = 28:

# 1. result = [], columnNumber = 28
# 2. 28 - 1 = 27, 27 % 26 = 1 -> chr(1 + 65 = 'B', result = ['B'], 27 // 26 = 1
# 3. 1 - 1 = 0, 0 % 26 = 0 -> chr(0 + 65) = 'A', result = ['B', 'A'], 0 // 26 = 0
# 4. Цикл завершён.
# 5. ''.join(reversed(['B', 'A'])) =  ''.join(['A', 'B']) = 'AB'

# Заводим экземпляр класса, выводим пример в консоль.
sol = Solution()

print("Пример работы для columnNumber (номер столбца) = 28:\n")
print("1. result = [], columnNumber = 28")
print("2. 28 - 1 = 27, 27 % 26 = 1 -> chr(1 + 65) = 'B', result = ['B'], 27 // 26 = 1")
print("3. 1 - 1 = 0, 0 % 26 = 0 -> chr(0 + 65) = 'A', result = ['B', 'A'], 0 // 26 = 0")
print("4. Цикл завершён.")
print("5. ''.join(reversed(['B', 'A'])) = ''.join(['A', 'B']) = 'AB'")
print("\nРезультат выполнения функции:", sol.convertToTitle(28))