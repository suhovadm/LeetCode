# Majority Element

# Дан массив nums размером n. Верните элемент, встречающийся большинство раз.

# Элементом большинства называется элемент, который встречается в массиве более [n / 2] раз.
# Можно предположить, что элемент большинства всегда существует в массиве.

# Пример 1:
# Входные данные: nums [3,2,3]
# Результат: 3

# Пример 2:
# Входные данные: nums = [2,2,1,1,1,2,2]
# Результат: 2

# Ограничения:
# n - длина массива nums.
# 1 <= n <= 5 * 10⁴
# -10⁹ <= nums[i] <= 10⁹
# Входные данные сгенерированы так, что элемент большинства гарантированно
# будет существовать в массиве.

# Импортируем тип List из модуля typing.
# Это необходимо для использования аннотации List[int], которая указывает,
# что параметр nums должен быть списком целых чисел.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод majorityElement, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. Ничего не делает в нашем случае.
    # nums: List[int] - принимает список целых чисел.
    # -> int - возвращает целое число.
    # Данная конструкция говорит нам о том, что ожидается список целых чисел на входе и целое число на выходе.
    def majorityElement(self, nums: List[int]) -> int:

    # Заводим переменную count, которая равна нулю.
    # Она будет использоваться для подсчёта "силы" текущего кандидата в элементе большинства.
        count = 0
    # Заводим переменную candidate со значением None.
    # В ней будет храниться потенциальный кандидат на элемент большинства.
    # Начальное значение None означает, что кандидат ещё не выбран.
        candidate = None

        # Запускаем цикл, который последовательно перебирает все элементы
        # списка nums. На каждой итерации переменная num принимает значение
        # очередного элемента массива.
        for num in nums:

        # Проверяем, равен ли счётчик нулю. Если да, это означает, что текущий
        # кандидат либо ещё не выбран (в начале), либо его "сила" исчерпана, и пора
        # выбрать нового кандидата.
            if count == 0:
        # Назначаем текущий элемент num новым кандидатом на элемент большинства.
                candidate = num

        # Обновляем значение счётчика:
        # если текущий элемент num совпадает с текущим кандидатом candidate, то увеличиваем
        # count на 1 (усиливаем кандидата).
        # Иначе (элемент отличается от кандидата) уменьшаем count на 1 (ослабляем кандидата,
        # как бы "взаимно уничтожая" пару разных элементов).
            count += (1 if num == candidate else -1)

        # После завершения цикла возвращаем значение candidate.
        # По условию задачи гарантируется, что элемент большинства существует, поэтому
        # после обработки всех элементов candidate будет содержать именно этот элемент.
        return candidate

# Сложность.
# Время: O(n) - один проход по массиву.
# Память: O(1) - используется только две вспомогательные переменные.

# Точка входа в программу, вывод примеров в консоль.
if __name__ == "__main__":

    sol = Solution()

    # Пример 1.
    nums1 = [3,2,3]
    print(f'Input: nums = {nums1}')
    print(f'Output: {sol.majorityElement(nums1)}')

    # Пример 2.
    nums2 = [2,2,1,1,1,2,2]
    print(f'Input: nums = {nums2}')
    print(f'Output: {sol.majorityElement(nums2)}')