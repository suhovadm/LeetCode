# Дана строка s, состоящая из слов и пробелов. Верните длину последнего слова в строке.
# Слово - это максимальная подстрока, состоящая только из символов, не являющихся пробелами.

# Пример 1:
# Ввод: s = "Hello World"
# Вывод: 5
# Объяснение: Последнее слово - "World" диной 5.

# Пример 2:
# Ввод: s = "fly me to the moon"
# Вывод: 4
# Объяснение: Последнее слово - "moon" диной 4.

# Пример 3:
# Ввод: s = "luffy is still joyboy"
# Вывод: 6
# Объяснение: Последнее слово - "joyboy" длиной 6.

# --- Максимально простой вариант для решения задачи:
class Solution:
    def lengthOfLastWord(self, s: str) -> int:

        words = s.split()

        return len(words[-1])

# --- Если нужно решить задачу без использования встроенного split():

# Этот вариант тоже простой:
# 1. Идём с конца строки, пропуская пробелы.
# 2. Затем считаем символы, пока не встретим пробел или начало строки.

class Solution:
    def lengthOfLastWord(self, s: str) -> int:

        length = 0
        i = len(s) - 1

        # Пропускаем пробелы в конце строки.
        while i >= 0 and s[i] == ' ':
            i -= 1

        # Считаем длину последнего слова.
        while i >= 0 and s[i] != ' ':
            i -= 1

        return length
