# Вы поднимаетесь по лестнице. Чтобы достичь вершины, чтобы преодолеть n ступенек.

# Кождый раз вы можете сделать либо 1, либо 2 шага (ступеньки). Сколькими различными способами
# вы можете подняться наверх ?

# Пример 1:
# Ввод: n = 2
# Вывод: 2
# Объяснение: есть два способа подняться наверх.

# 1. 1 шаг + 1 шаг
# 2. 2 шага

# Пример 2:
# Ввод: n = 3
# Вывод: 3
# Объяснение: есть три способа подняться наверх.

# 1. 1 шаг + 1 шаг + 1 шаг
# 2. 1 шаг + 2 шага
# 3. 2 шага + 1 шаг

# Заводим класс Solution.
class Solution:
    # Заводим метод climbStairs, который принимает целое число n и возвращает целое число.
    def climbStairs(self, n: int) -> int:

        # Если всего 1 ступенька - только 1 способ подняться (1 шаг).
        if n == 1:
            return 1

        # Если 2 ступеньки - 2 способа (1 + 1 или 2 шага).
        if n == 2:
            return 2

        # Создаём список из (n + 1) нулей.
        # Индекс в списке соответствует количеству ступенек.
        # ways[i] будет хранить количество способов добраться до i-й ступеньки.
        ways = [0] * (n + 1)

        # 1 способ для 1 ступеньки.
        ways[1] = 1
        # 2 способа для 2 ступенек.
        ways[2] = 2

        # Начиная с 3-й ступеньки, вычисляем количество способов:
        # способы добраться до i-й ступеньки =
        # способы добраться до (i-1)-й ступеньки +
        # способы добраться до (i-2)-й ступеньки
        for i in range(3, n + 1):
            ways[i] = ways[i - 1] + ways[i - 2]

        # Возвращаем количество способов для n ступенек.
        return ways[n]

# Объяснение:

# 1. Базовые случаи:
# Для 1 ступеньки: только 1 способ (1 шаг)
# Для 2 ступенек: 2 способа (1 + 1 или 2 шага)

# 2. Рекуррентная формула:
# Чтобы добраться до ступеньки i, можно:
# прийти с ступеньки i-1 (сделать 1 шаг)
# прийти с ступеньки i-2 (сделать 2 шага)
# Поэтому: ways[i] = ways[i-1] + ways[i-2]

# 3. Пример для n = 3:
# ways[1] = 1
# ways[2] = 2
# ways[3] = ways[2] + [1] = 2 + 1 = 3

# 4. Пример для n = 4:
# ways[1] = 1
# ways[2] = 2
# ways[3] = 3
# ways[4] = ways[3] + ways[2] = 3 + 2 = 5

