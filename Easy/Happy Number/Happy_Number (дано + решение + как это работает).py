# Happy Number

# Напишите алгоритм, чтобы определить, является ли число n счастливым.
# Счастливое число - это число, определяемое следующим процессом:

# 1. Начиная с любого положительного целого числа, замените это число суммой
# квадратов его цифр.

# 2. Повторяйте процесс до тех пор, пока число не станет равным 1 (где оно останется),
# или пока оно не войдёт в бесконечный цикл, который не включает 1.

# 3. Числа, для которых этот процесс заканчивается на 1, являются счастливыми.

# Верните true, если n является счастливым числом, и false в противном случае.

# Пример 1:
# Вход: n = 19
# Выход: true
# Объяснение:
# 1² + 9² = 82
# 8² + 2² = 68
# 6² + 8² = 100
# 1² + 0² + 0² = 1

# Пример 2:
# Вход: n = 2
# Выход: false

class Solution:
    def isHappy(self, n: int) -> bool:

        def get_next(number):

            total_sum = 0

            while number:

                digit = number % 10
                total_sum += digit * digit
                number //= 10

            return total_sum

        slow = n
        fast = get_next(n)

        while fast != 1 and slow != fast:
            slow = get_next(slow)
            fast = get_next(get_next(fast))

        return fast == 1

# Этот код использует алгоритм Флойда (Floyd's Cycle Detection Algorithm),
# также известный как "черепаха и заяц".
# Его применяют для обнаружения цикла в последовательности без дополнительной памяти.

# Как это работает в коде?
# Функция get_next(number) вычисляет: следующее число = сумма квадратов цифр.
# Например:
# 19 → 1² + 9² = 82
# 82 → 8² + 2² = 68
# 68 → 6² + 8² = 100
# 100 → 1² + 0² + 0² = 1
# Если последовательность приходит к 1, число - счастливое.
# Если нет, то она зацикливается (например: 4 -> 16 -> 37 -> ... -> 4).

# Где здесь алгоритм Флойда?
# Используются два указателя:
# slow = n
# fast = get_next(n)

# Дальше:
# slow = get_next(slow) # движется на 1 шаг
# fast = get_next(get_next(fast)) # движется на 2 шага
# Это классическая схема Floyd cycle detection:
# - если есть цикл, slow == fast,
# - если дошли до 1, число счастливое.

# Условие цикла:
# while fast != 1 and slow != fast:

# Логика выхода:
# return fast == 1
# - true -> дошли до 1,
# - false -> обнаружен цикл.