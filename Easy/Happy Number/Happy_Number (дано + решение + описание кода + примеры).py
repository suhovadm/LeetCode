# Happy Number

# Напишите алгоритм, чтобы определить, является ли число n счастливым.
# Счастливое число - это число, определяемое следующим процессом:

# 1. Начиная с любого положительного целого числа, замените это число суммой
# квадратов его цифр.

# 2. Повторяйте процесс до тех пор, пока число не станет равным 1 (где оно останется),
# или пока оно не войдёт в бесконечный цикл, который не включает 1.

# 3. Числа, для которых этот процесс заканчивается на 1, являются счастливыми.

# Верните true, если n является счастливым числом, и false в противном случае.

# Пример 1:
# Вход: n = 19
# Выход: true
# Объяснение:
# 1² + 9² = 82
# 8² + 2² = 68
# 6² + 8² = 100
# 1² + 0² + 0² = 1

# Пример 2:
# Вход: n = 2
# Выход: false

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Заводим метод isHappy, которая принимает в себя ряд параметров.
    # n: int - целое число.
    # -> bool - которое должно вернуть логическое значение true, или false (если число счастливое, или нет).
    def isHappy(self, n: int) -> bool:

    # Внутри метода isHappy, заводм ещё один - get_next, вспомогательный метод.
    # Его задача вычислить следующее число в последовательности для алгоритма поиска счастливых чисел:
    # сумму квадратов цифр переданного ей аргумента number.
        def get_next(number):

        # Заводим переменную total_sum, в которой будет скапливаться сумма квадратов цифр числа.
        # Изначально всё равно нулю.
            total_sum = 0

        # Заводим бесконечный цикл while, который будет выполняться до тех пор, пока переменная
        # number не станет равна нулю.
        # В Python любое ненулевое число считается True, в логическом контексте, а ноль - False.
        # Этот цикл предназначен для последовательного перебора всех цифр числа.
        # На каждой итерации мы будем "отщипывать" последнюю цифру.
            while number:

        # Вычисляем последнюю цифру текущего значения number.
        # Оператор % (остаток от деления) находит остаток от деления числа на 10.
        # Например, если number = 123, то 123 % 10 = 3. Сохраняем эту цифру в переменную digit.
                digit = number % 10

        # К нашей накапливаемой сумме прибавляем квадрат выделенной цифры.
        # Это эквивалентно total_sum = total_sum + (digit * digit).
        # Например, если digit = 3, то к сумме добавится 9.
                total_sum += digit * digit

        # number //= 10 - избавляемся от последней цифры числа, которую мы только что обработали.
        # Оператор //= выполняет целочисленное деление (деление нацело) и присваивание.
        # Это эквивалентно number = number // 10.
        # В Python // отбрасывает дробную часть. Например, если было number = 123, то после
        # number //= 10 станет 12.
        # Цикл переходит к следующей итерации, чтобы обработать следующую цифру (теперь
        # последней будет 2, и так далее).
                number //= 10

        # Когда цикл while завершается (то есть number стало равно 0 и все цифры обработаны),
        # функция get_next возвращает накопленную сумму квадратов всех цифр исходного числа
        # number. Это и есть "следующее число" в последовательности.
            return total_sum

        # Заводим "медленный" указатель (slow). Он начинает с исходного числа n.
        slow = n
        # Заводим "быстрый" указатель (fast). Он делает первый шаг и переходит
        # следующему числу в последовательности (сумме квадратов цифр числа n).
        fast = get_next(n)

        # Запускаем цикл, который будет выполнятся до тех пор, пока одновременно
        # выполняются 2 условия:
        # fast != 1: быстрый указатель ещё не достиг числа 1.
        # slow != fast: медленный и быстрый указатели ещё не встретились. Их встреча
        # означает, что последовательность вошла в цикл и никогда не достигнет 1.
        # Если любое из этих условий нарушается (fast стал равен 1 ИЛИ slow встретился с fast),
        # цикл прекращается.
        while fast != 1 and slow != fast:

        # Медленный указатель делает один шаг. Он переходит к следующему числу в последовательности
        # (вычисляет сумму квадратов своих текущих цифр).
            slow = get_next(slow)
        # Быстрый указатель делает сразу два шага.
        # Сначала вычисляется get_next(fast) - это первый шаг.
        # Затем результат передаётся в get_next ещё раз - это второй шаг.
        # Таким образом, fast перемещается по последовательности в два раза быстрее, чем slow.
            fast = get_next(get_next(fast))

        # После выхода из цикла проверяется условие.
        # fast == 1 - это логическое выражение. Если fast равен 1, оно вернёт True.
        # Если цикл остановится из-за того, что указатели встретились (и fast не равен 1),
        # то выражение вернёт False.
        # Это значение и будет результатом работы всего метода isHappy.
        return fast == 1

# Что такое квадратный корень - это число умноженное само на себя 1 раз.

# Почему 1 - счастливое число, а 2 - нет?
# Счастливое число - это то, которое на выходе при любых раскладах даёт 1.
# 1 * 1 = 1, мы сразу получили 1, процесс закончился.
# 2 уходит в цикл и на выходе даёт 4, процесс закончился НЕ 1.

# Пример 1:
# Вход: n = 19
# 1² + 9² = 1 + 81 = 82 // откуда берутся 1 и 9 - это 19 разбитая на 1 и 9!
# 8² + 2² = 64 + 4 = 68
# 6² + 8² = 36 + 64 = 100
# 1² + 0² + 0² = 1 + 0 + 0 = 1
# Выход: true

# Пример 2:
# Вход: n = 2
# 2² = 4
# 4² = 16
# 1² + 6² = 1 + 36 = 37
# 3² + 7² = 9 + 49 = 58
# 5² + 8² = 25 + 64 = 89
# 8² + 9² = 64 + 81 = 145
# 1² + 4² + 5² = 1 + 16 + 25 = 42
# 4² + 2² = 16 + 4 = 20
# 2² + 0² = 4 + 0 = 4
# Выход: false