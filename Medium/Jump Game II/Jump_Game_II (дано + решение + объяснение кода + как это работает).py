# Jump Game II

# Дан массив целых чисел nums, индексированный с нуля, длины n. Изначально вы находитесь на
# позиции с индексом 0.

# Каждый элемент nums[i] обозначает максимальную длину прыжка вперёд с позиции i. Другими
# словами, если вы находитесь в позиции i, вы можете прыгнуть в любую позицию (i + j), где:

# * 0 <= j <= nums[i], и
# * i + j < n
# Ваша задача - вернуть минимальное количество прыжков, необходимое, чтобы достичь позиции n - 1.

# Гарантируется, что вы всегда можете добраться до последнего индекса.

# Пример 1:
# вход: nums = [2, 3, 1, 1, 4]
# выход: 2
# Объяснение:
# минимальное количество прыжков - 2.
# Сначала прыгаем на 1 шаг: с индекса 0 на 1, затем на 3 шага - до последнего индекса.

# Пример 2:
# вход: nums = [2, 3, 0, 1, 4]
# Выход: 2

# Ограничения:
# * 1 <= nums.length <= 10⁴
# * 0 <= nums[i] <= 1000
# * Гарантируется, что можно добраться до nums[n - 1]

# -------------------------------------------------------------------------------------------- #

# Импортируем List из модуля typing для аннотации типов.
# Он нужен, чтобы показать, что именно лежит в списке и какой тип данных мы возвращаем.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод jump, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. В нашем случае просто дефолтный параметр. Ничего не делает.
    # nums: List[int] - метод принимает список целых чисел nums и возвращает целое число.
    def jump(self, nums: List[int]) -> int:

    # jumps - счётчик совершённых прыжков.
        jumps = 0
    # end - граница текущего прыжка (позиция, до которой мы можем добраться за текущее количество прыжков).
        end = 0
    # farthest - самая дальняя позиция, которую мы можем достичь на текущем шаге.
        farthest = 0

    # Цикл проходит по всем индексам массива, кроме последнего.
    # Мы не рассматриваем последний элемент, потому что если мы дошли до него - задача решена.
        for i in range(len(nums) - 1):

    # Обновление максимальной дальности.
    # Для каждой позиции i вычисляем, как далеко можно прыгнуть: i + nums[i].
    # Обновляем farthest - это максимальное расстояние, которое можно достичь, используя
    # прыжки из всех просмотренных позиций в текущем "сегменте".
            farthest = max(farthest, i + nums[i])

    # Проверка достижения границы.
    # Проверяем, достигли ли мы конца текущего прыжка (границы end).
    # Если да, то нам нужно совершить следующий прыжок.
            if i == end:

    # Совершение прыжка.
    # Увеличиваем счётчик прыжков.
    # Устанавливаем новую границу end равной farthest - теперь это максимальная позиция,
    # которую мы можем достичь после текущего прыжка.
                jumps += 1
                end = farthest

    # Возвращаем минимальное количество прыжков, необходимых для достижения последнего индекса.
        return jumps

# Как это работает?
# Для примера [2, 3, 1, 1, 4]
# * На позиции 0 можем дойти до 2 - обновляем farthest.
# * Дошли до end - делаем прыжок.
# * Дальше расширяем диапазон и делаем второй прыжок.

# Почему это оптимально?
# Мы всегда выбираем максимально дальнюю достижимую позицию.
# Не перебираем все варианты.
# Время: O(n)
# Память: O(1)