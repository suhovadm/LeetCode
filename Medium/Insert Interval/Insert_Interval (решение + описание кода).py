# Импортируем List из модуля typing для использования аннотации типов.
# Это позволяет указать, что intervals - это список списков целых чисел.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод insert, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. В нашем случае просто дефолтный параметр.
    # intervals: List[List[int]] - исходный список интервалов, каждый интервал представлен списком [start, end]
    # newInterval: List[int] - новый интервал для вставки.
    # -> List[List[int]] - возвращаем новый список интервалов после вставки и слияния.
    def insert(self, intervals: List[List[int]], newInterval: List[int]) -> List[List[int]]:

    # Заводим пустой список, в который мы будем собирать результирующие интервалы.
        result = []
    # Индекс для прохода по intervals.
        i = 0
    # Общее количество интервалов в исходном списке.
        n = len(intervals)

    # Первый цикл while: добавляем интервалы, которые находятся полностью до newInterval.

    # Условие пока не дошли до конца списка и конец текущего интервала ( intervals[i][1] ) строго
    # меньше начала нового интервала ( newInterval[0] ), значит, текущий интервал целиком находится
    # слева от newInterval и не пересекается с ним.

    # Действия:
    # - добавляем intervals[i] в result без изменений,
    # - увеличиваем i на 1, переходя к следующему интервалу.
    # Пример:
    # если intervals = [[1,2],[3,5]], а newInterval = [6,7], то оба исходных интервала попадут
    # сюда и будут добавлены в результат.
        while i < n and intervals[i][1] < newInterval[0]:
            result.append(intervals[i])
            i += 1

    # Второй цикл while: объединяем все пересекающиеся интервалы.

    # Условие: пока не дошли до конца списка и начало текущего интервала ( intervals[i][0] ) меньше
    # или равно концу нового интервала ( newInterval[1] ), значит, текущий интервал пересекается с
    # newInterval (или касается его).

    # Действия:
    # - расширяем newInterval, чтобы он покрывал все встреченные пересекающиеся интервалы:
    # - новое начало - минимум из текущего начала newInterval и начала текущего интервала.
    # - новый конец - максимум из текущего конца newInterval и конца текущего интервала.
    # Увеличиваем i на 1, переходя к следующему интервалу.
    # Важно: после выхода из цикла newInterval содержит объединённый интервал, который покрывает
    # все пересекающиеся интервалы, а i указывает на первый интервал, который находится полностью
    # после объединённого интервала.
        while i < n and intervals[i][0] <= newInterval[1]:
            newInterval[0] = min(newInterval[0], intervals[i][0])
            newInterval[1] = max(newInterval[1], intervals[i][1])
            i += 1

    # Добавляем объединённый интервал в результат.
        result.append(newInterval)

    # Третий цикл while: добавляем оставшиеся интервалы, которые находятся после newInterval.
    # Пока не дошли до конца списка, добавляем все оставшиеся интервалы в result без изменений.
    # Эти интервалы начинаются после объединённого интервала, поэтому они не пересекаются.
        while i < n:
            result.append(intervals[i])
            i += 1

    # Возвращаем итоговый список интервалов, который содержит:
    # - интервалы, которые были до newInterval,
    # - объединённый интервал,
    # - интервалы, которые были после newInterval.
        return result