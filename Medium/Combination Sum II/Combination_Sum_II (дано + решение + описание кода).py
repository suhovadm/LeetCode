# Combination Sum II

# Дан набор чисел-кандидатов (candidates) и целевое число (target).
# Найдите все уникальные комбинации из candidates, в которых сумма чисел равна target.

# Каждое число из candidates может быть использовано в комбинации только один раз.

# Примечание: результирующий набор не должен содержать повторяющихся комбинаций.

# Пример 1:
# Входные данные: candidates = [10,1,2,7,6,1,5], target = 8

# Результат:
# [
# [1,1,6],
# [1,2,5],
# [1,7],
# [2,6]
# ]

# Пример 2:
# Входные данные: candidates = [2,5,2,1,2], target = 5
# Результат:
# [
# [1,2,2],
# [5]
# ]

# Импортируем List из модуля typing для аннотации типов.
# Это позволяет указать, что функция принимает и возвращает списки.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод combinationSum2, который принимает в себя ряд параметров.
    # self - ссыкла на экземпляр класса.
    # candidates: List[int] - входной список чисел-кандидатов.
    # target: int - целевая сумма.
    # -> List[List[int]] - метод возвращает список списков целых чисел.
    def combinationSum2(self, candidates: List[int], target: int) -> List[List[int]]:

    # Сортируем входной массив чисел по возрастанию. Это важно для:
    # 1. оптимизации - мы сможем прекращать поиск, когда число превышает оставшуюся сумму.
    # 2. Обработки дубликатов - одинаковые числа будут рядом.
        candidates.sort()

    # Создаём пустой список для хранения всех найденных уникальных комбинаций.
        result = []

    # Определяем внутреннюю рекурсивную функцию для backtracking:
    # remaining_target - сколько ещё нужно набрать до цели.
    # start - индекс, с которого начинаем перебор в текущем вызове.
    # current_combination - текущая собираемая комбинация.
        def backtrack(remaining_target: int, start: int, current_combination: List[int]):

    # Базовый случай: если оставшаяся цель = 0, значит мы набрали нужную сумму.
            if remaining_target == 0:
    # Добавляем копию текущей комбинации в результат. Используем copy(), чтобы сохранить текущее
    # состояние, т.к. оригинальный список будет изменяться при возврате из рекурсии.
                result.append(current_combination.copy())
    # Возвращаемся из текущего вызова функции.
                return

    # Если оставшаяся цель стала отрицательной, значит текущая комбинация превысила целевую сумму.
            if remaining_target < 0:
    # Прекращаем дальнейший поиск по этому пути.
                return

    # Перебираем все элементы от start до конца массива. start гарантирует нам, что каждый элемент
    # используется не более 1 раза (мы не возвращаемся к уже рассмотренным элементам).
            for i in range(start, len(candidates)):
    # Проверка на дубликаты:
    # i > start - гарантирует, что мы не на первом элементе итерации.
    # candidates[i] == candidates[i - 1] - текущий элемент равен предыдущему.
                if i > start and candidates[i] == candidates[i - 1]:
    # Пропускаем текущую итерацию, чтобы избежать дублирующихся комбинаций.
    # Например, если у нас есть еденицы подряд, мы не будем начинать новые
    # комбинации со второй еденицы.
                    continue

    # Оптимизация: если текущий элемент больше оставшейся цели, то все последующие
    # элементы (массив отсортирован) тоже будут больше.
                if candidates[i] > remaining_target:
    # Прерываем цикл полностью, т.к. дальнейший перебор не имеет смысла.
                    break

    # Добавляем текущий элемент в собираемую комбинацию.
                current_combination.append(candidates[i])

    # Рекурсивно вызываем функцию:
    # - уменьшаем remaining_target на значение добавленного элемента,
    # - увеличиваем start до i + 1, чтобы не использовать текущий элемент повторно,
    # - передаём текущую комбинацию.
                backtrack(remaining_target - candidates[i], i + 1, current_combination)

    # После возврата из рекурсии удаляем последний добавленный элемент (backtracking), чтобы
    # попробовать другие варианты.
                current_combination.pop()

    # Запускаем рекурсивный процесс с начальными значениями:
    # remaining_target = target - нужно набрать всю целевую сумму,
    # start = 0 - начинаем с первого элемента,
    # current_combination = [] - пустая начальная комбинация.
        backtrack(target, 0, [])

    # Возвращаем все найденные уникальные комбинации.
        return result