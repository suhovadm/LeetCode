# Gray Code.

# Последовательность n-битного кода Грея - это последовательность из 2n целых чисел, в которой:

# * Каждое число находится в диапазоне [0, 2n - 1],
# * Первое число равно 0,
# * Каждое число встречается не более одного раза,
# * Двоичное представление каждой пары соседних чисел отличается ровно на один бит,
# * Двоичное представление первого и последнего чисел также отличается ровно на один бит.

# Дано целое число n. Верните любую корректную последовательность n-битного кода Грея.

# Пример 1:
# Вход: n = 2
# Выход: [0, 1, 3, 2]

# Объяснение:
# двоичное представление последовательности [0, 1, 3, 2] - это [00, 01, 11, 10].

# 00 и 01 отличаются на один бит.
# 01 и 11 отличаются на один бит.
# 11 и 10 отличаются на один бит.
# 10 и 00 отличаются на один бит.

# Также корректной является последовательность [0, 2, 3, 1], её двоичное представление: [00, 10, 11, 01].
# * 00 и 10 отличаются на один бит.
# * 10 и 11 отличаются на один бит.
# * 11 и 01 отличаются на один бит.
# * 01 и 00 отличаются на один бит.

# Пример 2:
# Вход: n = 1
# Выход: [0, 1]

# Ограничения:
# * 1 ≤ n ≤ 16

# Импортируем тип List из модуля typing.
# Это нужно для аннотации типов возвращаемого значения.
# Аннотация -> List[int] говорит, что метод возвращает список целых чисел.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод grayCode, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса.
    # n: int - метод принимает целое число (количество битов).
    # -> List[int] - возвращает список целых чисел, т.е. последовательность кодов Грея длиной 2n.
    def grayCode(self, n: int) -> List[int]:

    # Генерация кодов Грея.
    # a)
    # 1 << n - это побитовый сдвиг влево. 1 << n означает 2n.
    # Например, если n = 3, то 1 << 3 = 1000 в двоичной = 8.
    # Это количество чисел в последовательности кодов Грея для n битов.

    # b)
    # range(1 << n)
    # range(8) создаёт последовательность чисел от 0 до 7 включительно (всего 8 чисел).

    # c)
    # i ^ (i >> 1) - формула преобразования в код Грея.
    # Это известная формула: двоичный код Грея получается из обычного двоичного числа i
    # с помощью операции:
    # gray = i 0 (i >> 1)
    # где 0 - побитовое XOR (исключающее ИЛИ), а >> - побитовый сдвиг вправо.

    # d)
    # Списковое включение (list comprehension)
    # [i ^ (i >> 1) for i in range(1 << n)]
    # Создаёт список, где для каждого i от 0 до 2n - 1 вычисляется код Грея и добавляется в результат.

    # return ... - возврат результата.
    # Метод возвращает полученный список.
        return [i ^ (i >> 1) for i in range(1 << n)]

    # Итог:
    # Код генерирует код Грея для n битов с помощью формулы:
    # G(i) = i 0 (i >> 1)
    # Это стандартный способ получения бинарного кода Грея.

