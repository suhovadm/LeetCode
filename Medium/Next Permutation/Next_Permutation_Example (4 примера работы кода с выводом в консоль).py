if __name__ == "__main__":
    solution = Solution()

    # Пример 1: [1, 2, 3] -> [1, 3, 2]
    nums1 = [1, 2, 3]
    print(f'Исходный массив: {nums1}')
    solution.nextPermutation(nums1)
    print(f'Следующая перестановка: {nums1}')
    print()

    # Пример 1: [1, 2, 3] -> [1, 3, 2]
    # Находим i = 1 (число 2), т.к. 2 < 3
    # Находим j = 2 (число 3), т.к. 3 > 2
    # Меняем 2 и 3: [1, 3, 2]
    # Реверс хвоста (с индекса 2) ничего не меняет.

# ---------------------------------------------------------------------------- #

    # Пример 2: [3, 2, 1] -> [1, 2, 3]
    nums2 = [3, 2, 1]
    print(f'Исходный массив: {nums2}')
    solution.nextPermutation(nums2)
    print(f'Следующая перестановка: {nums2}')
    print()

    # Пример 2: [3, 2, 1] -> [1, 2, 3]
    # i становится -1 (весь массив по убыванию)
    # Блок замены пропускается
    # Реверс всего массива: [1, 2, 3]

# ---------------------------------------------------------------------------- #

    # Пример 3: [1, 1, 5] -> [1, 5, 1]
    nums3 = [1, 1, 5]
    print(f'Исходный массив: {nums3}')
    solution.nextPermutation(nums3)
    print(f'Следующая перестановка: {nums3}')
    print()

    # Пример 3: [1, 1, 5] -> [1, 5, 1]
    # Находим i = 1 (число 1), т.к. 1 < 5
    # Находим j = 2 (число 5), т.к. 5 > 1
    # Меняем 1 и 5: [1, 5, 1]
    # Реверс хвоста (с индекса 2) ничего не меняет.

# ---------------------------------------------------------------------------- #

    # Пример 4: [1, 5, 8, 4, 7, 6, 5, 3, 1] -> [1, 5, 8, 5, 1, 3, 4, 6, 7]
    nums4 = [1, 5, 8, 4, 7, 6, 5, 3, 1]
    print(f'Исходный массив: {nums4}')
    solution.nextPermutation(nums4)
    print(f'Следующая перестановка: {nums4}')
    print()

    # Пример 4: [1, 5, 8, 4, 7, 6, 5, 3, 1] -> [1, 5, 8, 5, 1, 3, 4, 6, 7]
    # Находим i = 3 (число 4), т.к. 4 < 7
    # Находим j = 6 (число 5), т.к. 5 > 4 (первое справа число больше 4)
    # Меняем 4 и 5: [1, 5, 8, 5, 7, 6, 4, 3, 1]
    # Реверс хвоста [7, 6, 4, 3, 1] -> [1, 3, 4, 6, 7]
    # Итог: [1, 5, 8, 5, 1, 3, 4, 6, 7]