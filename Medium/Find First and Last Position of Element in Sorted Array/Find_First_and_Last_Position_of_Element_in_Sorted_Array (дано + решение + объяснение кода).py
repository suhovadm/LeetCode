# Find First and Last Position of Element in Sorted Array

# Дан массив целых чисел nums, отсортированный в неубывающем порядке.
# Необходмо найти начальную и конечную позицию заданного целевого значения target.

# Если target не найден в массиве, верните [-1, -1].

# Вы должны написать алгоритм с временной сложностью выполнения O(log n).

# Пример 1:
# Входные данные: nums [5,7,7,8,8,10], target = 8
# Выходные данные: [3,4]

# Пример 2:
# Входные данные: nums = [5,7,7,8,8,10], target = 6
# Выходные данные: [-1, -1]

# Пример 3:
# Входные данные: nums = [], target = 0
# Выходные данные: [-1, -1]

# Ограничения:
# 0 <= nums.length <= 10^5
# -10^9 <= nums[i] <= 10^9
# Массив nums отсортирован в неубывающем порядке.
# -10^9 <= target <= 10^9

# Импортируем тип List из модуля typing для аннотации типов.
# Это позволяет указать, что функция принимает список целых чисел.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:

    # Определяем метод searchRange, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса.
    # nums: List[int] - отсортированный массив целых чисел.
    # target: int - искомое значение.
    # -> List[int] - метод возвращает список из двух целых чисел - начало и конец диапазона.
    def searchRange(self, nums: List[int], target: int) -> List[int]:

        # Вызываем вспомогательный метод findBound с параметром isFirst = True,
        # чтобы найти первое вхождение target в массиве. Результат сохраняем в переменную left.
        left = self.findBound(nums, target, True)

        # Если findBound вернул -1 (значит target не найден в массиве), сразу возвращаем [-1, -1]
        # согласно условию задачи.
        if left == -1:
            return [-1, -1]

        # Вызываем findBound с параметром isFirst = False, чтобы найти последнее вхождение target.
        # Результат сохраняем в переменную right.
        right = self.findBound(nums, target, False)

        # Возвращаем найденный диапазон [первая позиция, последняя позиция].
        return [left, right]

    # Вспомогательный метод для бинарного поиска границы.
    # isFirst = True - ищем левую границу (первое вхождение).
    # isFirst = False - ищем правую границу (последнее вхождение).
    # Возвращаем индекс найденного элемента или -1.
    def findBound(self, nums: List[int], target: int, isFirst: bool) -> int:
        # Заводим границы поиска:
        # left = 0 - начало массива.
        # right = длина массива - 1 - конец массива.
        left, right = 0, len(nums) - 1
        # Заводим результат значением -1 (на случай, если target не найден).
        result = -1

        # Начинаем цикл бинарного поиска. Продолжаем, пока левая граница не превысит правую.
        while left <= right:
            # Вычисляем средний индекс безопасным способом:
            # Обычное вычисление, которое было изначально: (left + right) // 2 может привести
            # к переполнению при больших числах.
            # Данная формула предотвращает переполнение:
            mid = left + (right - left) // 2

            # Если нашли target на позиции mid:
            if nums[mid] == target:
                # запоминаем эту позицию как возможный результат.
                result = mid

            # Если ищем первое вхождение:
                if isFirst:
            # сдвигаем правую границу влево, чтобы продолжить поиск в левой половине.
            # Это позволяет найти самое левое вхождение.
                    right = mid - 1
            # Если ищем последнее вхождение:
                else:
            # сдвигаем левую границу вправо, чтобы продолжить поиск в правой половине.
            # Это позволяет найти самое правое вхождение.
                    left = mid + 1

            # Если текущий элемент меньше target:
            elif nums[mid] < target:
            # значит target может быть только справа, сдвигаем левую границу.
                left = mid + 1
            # Если текущий элемент больше target:
            else:
            # значит target может быть только слева, сдвигаем правую границу.
                right = mid - 1

        # Возвращаем найденную позицию (или -1, если target не найден).
        return result