# Pow(x, n)

# Реализуйте функцию pow(x, n), которая вычисляет число x, возведённое в степень n (то есть x в степени n).

# Пример 1:
# вход: x = 2.00000, n = 10
# выход: 1024.00000

# Пример 2:
# вход: x = 2.10000, n = 3
# выход: 9.26100

# Пример 3:
# вход: x = 2.00000, n = -2
# выход: 0.25000
# объяснение: 2 в -2 степени = 1/2 в 2 степени = 1/4 = 0.25

# Ограничения:
# -100.0 < x < 100.0
# -2 в 31 степени больше или равно n больше или равно 2 в 31 степени -1
# n - целое число
# Либо x не равно нулю, либо n > 0
# -10 в 4 степени больше или равно x в n-ной степени больше или равно 10 в четвёртой степени.

# -------------------------------------------------------------------------------------------

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод myPow, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса, дефолтный параметр.
    # x: float - основание степени, дробное число.
    # n: int - показатель степени, целое число. Может быть положительным, отрицательным, или нулём.
    # -> float - возврат может быть дробным числом.
    def myPow(self, x: float, n: int) -> float:

    # Обработка отрицательной степени.
    # Если n отрицательный, например myPow(2, -3), то по определению:
    # 2 в минус -3 степени = 1/2 в 3 степени.
        if n < 0:
            # Заменяем x на 1 / x (теперь считаем для положительного показателя).
            x = 1 / x
            # Делаем n = -n, чтобы далее работать с положительными n.
            # Пример: x = 2, n = -3 - после преобразования x = 0.5, n = 3.
            n = -n

    # Заводим результат.
    # Начальное значение результата = 1.
    # При возведении в степень мы будем накапливать произведение.
    # 1.0 - тип float (чтобы избежать целочисленного деления, хотя здесь умножение).
        result = 1.0

    # Заглавный цикл бинарного возведения в степень.
    # Пока n не станет нулём мы будем обрабатывать биты числа n справа налево (от младшего к старшему).
        while n > 0:
    # Проверка текущего бита.
    # n % 2 - остаток от деления на 2, т.е. младший бит числа n.
    # Если он равен 1, значит текущий разряд участвует в разложении степени.
    # Домножаем result на текущее значение x (которое соответствует x^(2^k) для текущего шага).
            if n % 2 == 1:
                result *= x

    # Подготовка к следующему биту.
    # Возводим x в квадрат.
    # Если на i-м шаге x представлял x^(2^i), то после возведения в квадрат он будет представлять
    # x^(2^(i+1)) для следующего бита.
            x *= x
    # Сдвиг битов.
    # Целочисленное деление на 2 - отбрасываем младший бит, который мы только что обработали.
    # Это эквивалентно сдвигу вправо на 1 бит.
            n //= 2

    # Возврат результата. Возвращаем накопленное произведение.
        return result

# Как это работает?
# Мы разбиваем степень на биты (двоичное представление n).
# Если бит равен 1 - умножаем результат на текущий x.
# Каждый шаг: x = x * x, n = n // 2.

