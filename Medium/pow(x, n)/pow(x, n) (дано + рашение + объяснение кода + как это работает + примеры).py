if __name__ == "__main__":
    solution = Solution()

    # Пример 1:
    # вход: x = 2.00000, n = 10
    # выход: 1024.00000
    print(f'Пример 1: {solution.myPow(2.00000, 10):.5f}')

    # Пример 2:
    # вход: x = 2.10000, n = 3
    # выход: 9.26100
    print(f'Пример 2: {solution.myPow(2.10000, 3):.5f}')

    # Пример 3:
    # вход: x = 2.00000, n = -2
    # выход: 0.25000
    # объяснение: 2 в -2 степени = 1/2 в степени = 1/4 = 0.25
    print(f'Пример 3: {solution.myPow(2.00000, -2):.5f}')