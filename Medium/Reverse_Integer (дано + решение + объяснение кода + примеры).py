 # Дано 32-разрядное число со знаком x. Верните число, состоящее из цифр x в обратном
# порядке. Если при развороте цифр значение выходит за пределы диапазона 32-разрядных
# целых чисел со знаком [-2³¹, 2³¹ - 1], то верните 0.

# Предполагается, что в данной среде невозможно хранить 64-разрядные целые числа
# (со знаком или без знака).

# Пример 1:
# Ввод: x = 123
# Вывод: 321

# Пример 2:
# Ввод: x = -123
# Вывод: -321

# Пример 3:
# Ввод: x = 120
# Вывод: 21

# Ограничения:
# -2³¹ <= x <= 2³¹ - 1

# Заводим класс Solution, как того требует LeetCode.
class Solution:

    # Заводим метод reverse, который принимает в себя ряд параметров.
    # self - ссылка на объект класса (обязательный параметр для методов класса. В нашем случае он пуст).
    # x: int - указываем, что входной параметр x должен быть целым числом (int).
    # -> int - указываем, что метод возвращает целое число (int).
    def reverse(self, x: int) -> int:

        # Определяем знак числа.
        # Если x < 0 (число отрицательное), то sign = -1.
        # Иначе (число положительное или ноль), то sign = 1.
        # Пример: для x = -123 -> sign = -1.
        sign = -1 if x < 0 else 1

        # Берём абсолютное значение (модуль) числа:
        # abs() убирает знак минус.
        # Теперь в переменной x хранится только величина числа без знака.
        # Пример: было x = -123 -> стало x = 123.
        x = abs(x)

        # Разворот числа (основная строка в коде).
        # str(x) - преобразуем число в строку.
        # Пример: 123 -> "123".
        # [::-1] - срез строки в обратном порядке.
        # Пример: "123" - "321".
        # int(...) - преобразуем обратно в целое число.
        # Пример: "321" - 321. + Убираем все ведущие нули, если они есть.
        # * sign - возвращаем знак числа.
        # Умножаем на 1 (если было положительное) или на -1 (если было отрицательное).
        # Пример: 321 * (-1) -> -321.
        reversed_num = int(str(x)[::-1]) * sign

        # Проверяем, не выходит ли результат за пределы 32-битного целого числа.
        # -2**31 = -2147483648 - минимальное значение для 32-битного int-a.
        # 2**31 - 1 = 2147483647 - максимальное значение для 32-битного int-a.
        # Условие: если результат МЕНЬШЕ минимума ИЛИ БОЛЬШЕ максимума.
        if reversed_num < -2**31 or reversed_num > 2**31 - 1:

            # Если условие выше выполнилось (число вне диапазона), возвращаем 0.
            return 0

        # Если число в допустимом диапазоне, возвращаем развёрнутое число.
        return reversed_num

# Создаём объект sol класса Solution и запускаем код в консоль.
sol = Solution()

print('Примеры.')
print('Вход: 123', 'Выход:', sol.reverse(123)) # 321
print('Вход: -123', 'Выход:', sol.reverse(-123)) # -321
print('Вход: 120', 'Выход:', sol.reverse(120)) # 21. Разворачиваем строку в 021. Функция int() автоматически отбрасывает ведущие нули.
print('Вход: 0', 'Выход:', sol.reverse(0)) # 0
print('Вход: 1534236469', 'Выход:', sol.reverse(1534236469)) # 0, потому что выход за пределы 32-битного диапазона.
