# Multiply String ( Перемножение строк ).

# Условие задачи:

# Даны два неотрицательных целых числа num1 и num2, представленных в виде строк.
# Верните произведение num1 и num2, также представленное в виде строки.

# Примечание: запрещено использовать какие-либо встроенные библиотеки для работы
# с большими числами (BigInteger) или преобразовывать входные данные непосредственно
# в целые числа.

# Пример 1:
# Вход: num1 = "2", num2 = "3"
# Выход: "6"

# Пример 2:
# Вход: num1 = "123", num2 = "456"
# Выход: "56088"

# Ограничения:
# 1 <= num1.length, num2.length <= 200
# num1 и num2 состоят только из цифр.
# Ни num1, ни num2 не содержат ведущих нулей, за исключением самого числа 0.

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод multiply, который принимает в себя ряд параметров.
    # num1: первое число в виде строки.
    # num2: второе число в виде строки.
    # -> str - возвращаем результат умножения в виде строки.
    def multiply(self, num1: str, num2: str) -> str:

        # Проверяем особый случай: если любое из чисел равно "0",
        # то произведение будет равно "0".
        # Сразу возвращаем "0", чтобы не выполнять лишних действий.
        if num1 == "0" or num2 == "0":
            return "0"

        # Преобразуем строки в списки цифр и сразу разворачиваем их (делаем reversed order):
        # num1[::-1] - разворачиваем строку, чтобы младший разряд оказался на позиции 0.
        # int(digit) - преобразует каждый символ в целое число.
        # Например: "123" -> [3, 2, 1].
        num1 = [int(digit) for digit in num1[::-1]]
        num2 = [int(digit) for digit in num2[::-1]]

        # Создаём массив для результата. Максимальная длина произведения двух чисел
        # не превышает сумму длин множителей. Например: 99х99=9801 (2+2=4 цифры).
        result = [0] * (len(num1) + len(num2))

        # Внешний цикл проходит по каждой цифре первого числа (начиная с младшего разряда).
        for i in range(len(num1)):

            # Внутренний цикл проходит по каждой цифре второго числа.
            for j in range(len(num2)):

                # Перемножаем текущие цифры из первого и второго числа.
                # Например: для 123x456 на первом шаге i=0 (цифра 3), j=0 (цифра 6) -> 3x6 = 18.
                product = num1[i] * num2[j]

                # Добавляем произведение в соответствующую позицию.
                # Индекс i + j соответствует позиции, куда попадает данное произведение при умножении в столбик.
                result[i + j] += product

                # Обрабатываем перенос в следующий разряд:
                # result[i + j] // 10 - получаем количество десятков (цифру для переноса).
                # Добавляем это значение в следующий разряд i + j + 1.
                result[i + j + 1] += result[i + j] // 10

                # Оставляем в текущем разряде только остаток от деления на 10 (единицы).
                result[i + j] %= 10

        # Удаляем ведущие нули:
        # - пока длина массива больше 1 и последний элемент (старший разряд) равен 0
        # - удаляем последний элемент (pop).
        # Это нужно для случаев, когда результат короче, чем выделенная память.
        # Например: 2x3=6, а массив result = [6, 0, 0] -> после удаления ведущих нулей станет [6].
        while len(result) > 1 and result[-1] == 0:
            result.pop()

        # Формируем итоговую строку.
        # result[::-1] - разворачиваем массив обратно (теперь старший разряд слева).
        # map(str, ...) - преобразуем каждую цифру в строку.
        # ''.join(...) - объединяем все цифры в одну строку.
        # Возвращаем результат.
        return ''.join(map(str, result[::-1]))

# Подключаем экземпляр класса, выводим примеры в консоль.
solution = Solution()

# Пример 1: "2" x "3".
num1 = "2"
num2 = "3"
result = solution.multiply(num1, num2)
print(f'{num1} x {num2} = {result}')

# Пример 2: "123" x "456".
num1 = "123"
num2 = "456"
result = solution.multiply(num1, num2)
print(f'{num1} x {num2} = {result}')