# Spiral Matrix II.

# Учитывая положительное целое число n, создай матрицу размером n x n, заполненную элементами
# от 1 до n² в спиральном порядке.

# Пример 1:
# входные данные: n = 3
# результат: [[1,2,3],[8,9,4],[7,6,5]]

# Пример 2:
# входные данные: n = 1
# результат: [[1]]

# Ограничения:
# 1 <= n <= 20

# Импортируем List из модуля typing для аннотации типов.
# Это позволяет указать, что функция возвращает список списков.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод generateMatrix, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса.
    # n: int - целое число, размер квадратной матрицы n x n.
    # -> List[List[int]] - возвращает список списков целых чисел.
    def generateMatrix(self, n: int) -> List[List[int]]:

    # Создаём квадратную матрицу размером n x n, заполненную нулями.
    # [0] * n - создаём список из n нулей.
    # for _ in range(n) - повторяем это n количество раз, создавая n строк.
    # В итоге получается матрица вида: [[0,0,0], [0,0,0], [0,0,0] для n = 3.
        matrix = [[0] * n for _ in range(n)]

    # Заводим переменную num = 1, которая будет хранить текущее число для
    # вставки в матрицу начиная с 1.
        num = 1

    # Основной цикл по слоям матрицы. Количество слоёв равно (n+1) // 2:
    # для n = 3: (3+1) // 2 = 2 слоя,
    # для n = 4: (4+1) // 2 = 2 слоя (целочисленное деление даёт 2),
    # для n = 5: (5+1) // 2 = 3 слоя.
    # Каждый слой - это прямоугольный контур матрицы, который нужно заполнить.
        for layer in range((n + 1) // 2):

    # Заполнение верхней строки текущего слоя слева направо:
    # i проходит от layer до n-layer-1 (не включая n-layer).
    # matrix[layer[i] - фиксированная строка (layer), изменяется столбец (i).
    # Присваиваем текущее значение num и увеличиваем num на 1.
    # Пример для layer=0 при n=3: заполняем позиции [0][0], [0][1], [0][2].
            for i in range(layer, n - layer):
                matrix[layer][i] = num
                num += 1

    # Заполнение правого столбца текущего слоя сверху вниз:
    # i проходит от layer+1 до n-layer-1 (исключая верхний угол, который уже заполнен).
    # matrix[i][n-layer-1] - фиксированный столбец (крайний справа), изменяется строка (i).
    # Присваиваем значения и увеличиваем num.
    # Пример для layer=0 при n=3: заполняем [1][2], [2][2].
            for i in range(layer +1, n - layer):
                matrix[i][n - layer - 1] = num
                num += 1

    # Заполнение нижней строки текущего слоя справа налево:
    # i идёт от n-layer-2 вниз до layer-1 (шаг -1).
    # matrix[n-layer-1][i] - фиксированная нижняя строка, изменяется столбец.
    # Начинаем с предпоследнего элемента справа, чтобы не перезаписывать правый нижний угол.
    # Заканчиваем на layer (левый край).
    # Пример для layer=0 при n=3: заполняем [2][1], [2][0].
            for i in range(n - layer -2, layer -1, -1):
                matrix[n - layer - 1][i] = num
                num += 1

    # Заполнение левого столбца текущего слоя снизу вверх:
    # i идёт от n-layer-2 вниз до layer+1 (шаг -1).
    # matrix[i][layer] - фиксированный левый столбец, изменяется строка.
    # Начинаем с предполследней строки снизу, поднимаемся вверх.
    # Заканчиваем перед верхним левым углом (который уже заполнен в первом цикле).
    # Пример для layer=0 при n=3: этот цикл не выполняется, т.к. range(1, 0, -1) пустой.
            for i in range(n - layer -2, layer, -1):
                matrix[i][layer] = num
                num += 1

    # Возвращаем заполненную матрицу.
        return matrix

# --- Пример. ---

# Создаём экземпляр класса Solution.
solution = Solution()

result = solution.generateMatrix(3) # 3 - это значит, что матрица n = 3.

# Выводим матрицу построчно.
for row in result:
    print(row)

