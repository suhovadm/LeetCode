# Spiral Matrix II.

# Учитывая положительное целое число n, создай матрицу размером n x n, заполненную элементами
# от 1 до n² в спиральном порядке.

# Пример 1:
# входные данные: n = 3
# результат: [[1,2,3],[8,9,4],[7,6,5]]

# Пример 2:
# входные данные: n = 1
# результат: [[1]]

# Ограничения:
# 1 <= n <= 20

from typing import List

class Solution:
    def generateMatrix(self, n: int) -> List[List[int]]:

        matrix = [[0] * n for _ in range(n)]

        num = 1

        for layer in range((n + 1) // 2):

            for i in range(layer, n - layer):
                matrix[layer][i] = num
                num += 1

            for i in range(layer +1, n - layer):
                matrix[i][n - layer - 1] = num
                num += 1

            for i in range(n - layer -2, layer -1, -1):
                matrix[n - layer - 1][i] = num
                num += 1

            for i in range(n - layer -2, layer, -1):
                matrix[i][layer] = num
                num += 1

        return matrix

# Как это работает ?

# Классическое заполнение матрицы по спирали (по часовой стрелке).

# 1. Создаём пустую матрицу n x n, заполняем её нулями.
# Переменная num - это текущее число, которое мы будем записывать (от 1 до n²).

# 2. Идём слоями.
# Матрица заполняется кольцами: сначала внешний слой, потом следующий внутри и т.д.
# Количество слоёв: (n + 1) // 2.

# 3. Для каждого слоя выполняются 4 прохода:
#   - слева направо (верхняя строка): matrix[layer][i]
#   - сверху вниз (правый столбец): matrix[i][n - layer - 1]
#   - справа налево (нижняя строка): matrix[n - layer - 1][i]
#   - снизу вверх (левый столбец): matrix[i][layer]

# 4. После каждого присваивания увеличиваем num, чтобы числа шли по порядку.

# 5. Сужаем границы - переходим к следующему внутреннему слою.

# 6. В итоге получается спираль.
# Для n = 3:

# 1 2 3
# 8 9 4
# 7 6 5