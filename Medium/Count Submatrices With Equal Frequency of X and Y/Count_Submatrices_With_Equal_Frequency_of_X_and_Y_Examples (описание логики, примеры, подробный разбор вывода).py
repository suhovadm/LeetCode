from Count_Submatrices_With_Equal_Frequency_of_X_and_Y import *

# ----------------------------------------------------------------------------------------

# Логика:
# 1. Создаём префиксные суммы px и py, чтобы быстро знать, сколько 'X' и 'Y'
# в любой подматрице от (0,0) до (i,j).
# 2. Перебираем все правые нижние углы (i,j) подматрицы, которая начинается в (0,0).
# 3. Берём количество 'X' и 'Y' из префиксов.
# 4. Проверяем условия ( x == y и x > 0 ).
# 5. Если подходит - добавляем к результату.
# 6. Главное преимущество: каждую подматрицу проверяем за O(1),
# что даёт общую сложность O(n x m).

# ----------------------------------------------------------------------------------------

# --- Примеры для проверки. ---
solution = Solution()

grid1 = [["X", "Y", "."], ["Y", ".", "."]]
print("Пример 1:", solution.number_of_Submatrices(grid1)) # 3

grid2 = [["X", "X"], ["X", "Y"]]
print("Пример 2:", solution.number_of_Submatrices(grid2)) # 0

grid3 = [[".", "."], [".", "."]]
print("Пример 3:", solution.number_of_Submatrices(grid3)) # 0

# Пример вывода для grid1:

# Подматрица (0,0) -> (0,1):
# ['X', 'Y']
# X: 1, Y: 1

# Подматрица (0,0) -> (1,0):
# ['X']
# ['Y']
# X: 1, Y: 1

# Подматрица (0,0) -> (1,1):
# ['X', 'Y']
# ['Y', '.']
# X: 1, Y: 2   <-- эта не учитывается, выводятся только равные X и Y

# 1. Алгоритм считает префиксные суммы X и Y.
# 2. Для каждой подходящей подматрицы (равное число X и Y и хотя бы один X) делает вывод:
#   - координаты подматрицы (0,0) → (i,j)
#   - содержимое подматрицы
#   - количество X и Y
# 3. В конце возвращает общее количество подходящих подматриц.

# ----------------------------------------------------------------------------------------

# Подробный разбор вывода:

# 1. Условие задачи.
# Подматрица должна включать:
# 1.1. Включать клетку (0,0).
# 1.2. Иметь равное количество X и Y.
# 1.3. Иметь хотя бы один X.

# 2. Визуализируем grid1.
# 0 1 2 (индексы столбцов).
# 0 X Y .
# 1 Y . .
# Индексы строк: 0,1
# Индексы столбцов: 0,1,2

# 3. Рассмотрим все подматрицы, которые включают (0,0)
# 3.1. Любая подматрица начинается в (0,0).
# 3.2. Правая нижняя граница может быть (i,j):
# Возможные правые нижние углы:
# 3.3. (0,0) - подматрица:
# X
# X = 1, Y = 0 - не подходит (X не равно Y).
# 3.4. (0,1) - подматрица:
# X Y
# X = 1, Y = 1 - подходит! [V]
# 3.5. (0,2) - подматрица:
# X Y .
# X = 1, Y = 1 - подходит! [V]
# 3.6. (1,0) - подматрица:
# X
# Y
# X = 1, Y = 1 - подходит!
# 3.7. (1,1) - подматрица:
# X Y
# Y .
# X = 1, Y = 2 - не подходит (X не равно Y)
# 3.8. (1,2) - подматрица:
# X Y .
# Y . .
# X = 1, Y = 2 - не подходит!

# 4. Считаем все подходящие.
# Подходящие подматрицы (равное число X и Y и есть хотя бы один X):
# 4.1. (0,1) - [['X', 'Y']]
# 4.2. (0,2) - [['X', 'Y', '.']]
# 4.3. (1,0) - [['X', 'Y']]
# Итого: всего 3 подматрицы.

# 5. Почему алгоритм возвращает 3?
# Для каждой подматрицы алгоритм берёт: x = px[i+1][j+1] и y = py[i+1][j+1]
# Проверяет условие x == y and x > 0
# Если условие выполнено, увеличивает result += 1
# В нашем примере это происходит 3 раза - result = 3.