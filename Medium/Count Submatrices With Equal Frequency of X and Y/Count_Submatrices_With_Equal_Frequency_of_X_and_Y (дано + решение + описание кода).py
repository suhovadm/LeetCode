# Count Submatrices With Equal Frequency of X and Y

# Дан двумерный массив символов grid, где grid[i][j] - это либо 'X', 'Y', либо '.'.
# Необходимо вернуть количество подматриц, которые:

# - содержат элемент grid[0][0],
# - имеют равное количество символов 'X' и 'Y',
# - содержат хотя бы один символ 'X'.

# --- Пример 1:
# вход:
# grid = [["X", "Y", "."],
#        ["Y", ".", "."]]
# выход: 3

# --- Пример 2:
# вход:
# grid = [["X", "X"],
#        ["X", "Y"]]
# выход: 0
# Пояснение: ни одна подматрица не содержит равное количество 'X' и 'Y'.

# --- Пример 3:
# вход:
# grid = [[".", "."],
#        [".", "."]
# выход: 0
# Пояснение: ни одна подматрица не содержит хотя бы один символ 'X'.

# Ограничения:
# 1 <= grid.length, grid[i].length <= 1000
# grid[i][j] - это 'X', 'Y' или '.'

# Импортируем List из модуля typing для аннотации типов.
# Это нужно, чтобы было понятно, что grid - это список списков строк.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Объявляем метод numberOfSubmatrices, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса. В нашем случае ничего не делает.
    # Вход: grid - 2D список символов 'X', 'Y' или '.'. Список списков строк.
    # Выход: int - количество подходящих подматриц.
    def number_of_Submatrices(self, grid: List[List[str]]) -> int:

        # n - количество строк в матрице grid.
        # m - количество столбцов в матрице grid. [0] - считаем с первого элемента списка.
        # Это нужно для перебора всех ячеек и создания массивов префиксных сумм.
        n, m = len(grid), len(grid[0])

        # Создаём 2 массива префиксных сумм размера (n+1) x (m+1):
        # px - количество 'X' в прямоугольниках от (0,0) до любой клетки (i,j)
        # py - количество 'Y'
        # Почему n+1 и m+1 ?
        # Используем 1-базированную индексацию для удобства.
        # Так проще вычитать суммы прямоугольников без ошибок с индексами.
        # Изначально все элементы равны 0.
        px = [[0] * (m + 1) for _ in range(n + 1)]
        py = [[0] * (m + 1) for _ in range(n + 1)]

        # Перебираем каждую ячейку (i,j) в матрице grid.
        # i - индекс строки, j - индекс столбца.
        for i in range(n):
            for j in range(m):

        # Построение префиксных сумм.
        # px[i+1][j+1] - количество 'X' в прямоугольнике (0,0) - (i,j)
        # px[i][j+1] - количество 'X' выше текущей строки (до i-1)
        # px[i+1][j] - количество 'X' слева от текущего столбца (до j-1)
        # px[i][j] - вычитаем перекрытие, чтобы не считать дважды.
        # Аналогично для py с 'Y'.
        # Это классический метод 2D префиксных сумм:
        # сумма в любом прямоугольнике (0,0) - (i,j) вычисляется за O(1).
                px[i + 1][j + 1] = px[i][j + 1] + px[i + 1][j] - px[i][j]
                py[i + 1][j + 1] = py[i][j + 1] + py[i + 1][j] - py[i][j]

        # Добавляем значение текущей ячейки:
        # если 'X', увеличиваем px[i+1][j+1] на 1
        # если 'Y', увеличиваем py[i+1][j+1] на 1
        # если '.', ничего не делаем.
        # После этого px[i+1][j+1] и py[i+1][j+1] содержат полную сумму для прямоугольника (0,0) - (i,j).
                if grid[i][j] == 'X':
                    px[i + 1][j + 1] += 1
                elif grid[i][j] == 'Y':
                    py[i + 1][j + 1] += 1

        # Заводим счётчик подходящих подматриц.
        # Каждая подматрица, удовлетворяющая условиям, будет увеличивать этот счётчик.
        result = 0

        # Снова перебираем все ячейки (i,j)
        # На этот раз каждая ячейка (i,j) - это правый нижний угол подматрицы,
        # которая начинается в (0,0).
        # Благодаря условию задачи: подматрица всегда включает (0,0), поэтому
        # нам не нужно перебором выбирать левый верхний угол.
        for i in range(n):
            for j in range(m):

        # Получаем количество 'X' и 'Y' в прямоугольнке (0,0) - (i,j).
        # Используем готовые префиксные суммы, поэтому эта операция O(1).
                x = px[i + 1][j + 1]
                y = py[i + 1][j + 1]

        # Проверяем условия задачи:
        # 1. 'X' и 'Y' встречаются одинаковое количество раз ( x == y ).
        # 2. Есть хотя бы один 'X' ( x > 0 ).
        # Если условия выполнены - увеличиваем счётчик result.
                if x == y and x > 0:
                    result += 1

        # Возвращаем итоговое количество подматриц, удовлетворяющих всем условиям.
        return result