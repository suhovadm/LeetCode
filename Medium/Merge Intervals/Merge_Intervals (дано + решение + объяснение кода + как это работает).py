# Merge Intervals (Medium #56).

# Дан массив интервалов, где intervals[i] = [start_i, end_i].
# Необходимо объединить все пересекающиеся интервалы и вернуть массив непересекающихся
# интервалов, которые покрывают все интервалы из входных данных.

# Пример 1:
# вход: intervals = [[1,3], [2,6], [8,10], [15,18]]
# выход: [[1,6], [8,10], [15,18]]
# Объяснение: интервалы [1,3] и [2,6] пересекаются, поэтому объединяются в [1,6].

# Пример 2:
# вход: intervals = [[1,4], [4,5]]
# выход: [[1,5]]
# Объяснение: интервалы [1,4] и [4,5] считаются пересекающимися.

# Пример 3:
# вход: intervals = [[4,7], [1,4]]
# выход: [[1,7]]
# Объяснение: интервалы [1,4] и [4,7] считаются пересекающимися.

# Ограничения:
# 1 <= intervals.length <= 10⁴
# intervals[i].length == 2
# 0 <= start_i <= end_i <= 10⁴

# ------------------------------------------------------------------------------------

# Импортируем класс List из модуля typing.
# Это позволяет указывать на то, что переменная или параметр функции является
# списком с элементами определённого типа.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод merge, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса, дефолтный параметр для методов экземпляра.
    # intervals: List[List[int]] - аннотация типа: параметр intervals ожидает список,
    # каждый элемент которого - список из двух целых чисел ( List[int] ).
    # Фактически это список интервалов вида [start, end].
    # -> List[List[int]] - аннтоация возвращаемого типа: метод возвращает список
    # интервалов. Метод реализует стандартный алгоритм слияния пересекающихся интервалов.
    def merge(self, intervals: List[List[int]]) -> List[List[int]]:

    # Вызываем метод sort() у списка intervals.
    # Сортировка выполняется на месте (изменяет исходный список).
        intervals.sort()

    # Создаём пустой список merged. В него будут последовательно помещаться
    # результирующие интервалы. В процессе работы он всегда хранит объединённые
    # интервалы в порядке возрастания и без перекрытий.
        merged = []

    # Запускаем цикл по каждому интервалу в отсортированном списке intervals.
    # Переменная interval на каждой итерации получает очередной интервал - список из двух
    # чисел [start, end].
        for interval in intervals:
    # Условие проверяет, можно ли добавить текущий интервал как новый (без слияния) или
    # нужно объединить его с последним добавленным.
    # not merged - если список merged пуст (первая итерация), то условие истинно.
    # merged[-1][1] - последний элемент последнего интервала в merged, т.е. правый конец
    # последнего объединённого интервала.
    # interval[0] - левый конец текущего интервала.
    # Условие merged[-1][1] < interval[0] выполняется, когда последний объединённый интервал
    # заканчивается строго раньше, чем начинается текущий. В этом случае интервалы не
    # пересекаются (и не соприкасаются; если бы было <=, то соприкасающиеся интервалы [1,2] и
    # [3,4] не сливались бы, но обычно их тоже можно объединить, однако здесь условие == не
    # объединяет).
            if not merged or merged[-1][1] < interval[0]:
    # Текущий интервал добавляется в конец списка merged как новый самостоятельный интервал.
    # Это происходит в двух случаях:
    # - список merged был пуст,
    # - последний интервал в merged не перекрывается с текущим.
                merged.append(interval)
    # Ветка else выполняется, когда merged не пуст и merged[-1][1] >= interval[0].
    # Это означает, что последний объединённый интервал и текущий интервал пересекаются или
    # как минимум соприкасаются (если ==). В такой ситуации их нужно объединить.
            else:
    # Выполняем слияние: правый конец последнего интервала в merged заменяется на максимальное
    # значение из:
    # merged[-1][-1] - старый правый конец последнего интервала ( эквивалент merged[-1][1] ).
    # interval[1] - правый конец текущего интервала.
    # max - гарантирует, что объединённый интервал охватит оба исходных. Например:
    # последний [1,5], текущий [2,8] -> max(5,8)=8 -> результат [1,8].
    # Последний [1,5], текущий [4,3] (хотя по условию сортировки такого не будет, т.к. левые
    # концы возрастают) - max(5,3)=5 -> остаётся [1,5].
    # Важно: левая граница последнего интервала не меняется, потому что сортировка гарантирует,
    # что interval[0] >= merged[-1][0].
                merged[-1][1] = max(merged[-1][-1], interval[1])

    # После обработки всех интервалов, возвращаем результат работы метода.
        return merged

# Как это работает?
# Алгоритм сначала сортирует интервалы по началу, затем проходит по ним один раз, поддерживая
# текущий объединённый интервал. Если текущий интервал начинается после окончания предыдущего,
# он добавляется как новый; иначе выполняется расширение правой границы.