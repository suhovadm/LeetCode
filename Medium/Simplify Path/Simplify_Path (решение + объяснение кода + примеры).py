# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Заводим метод simplifyPath, который принимает
    # строку с путём и возвращает строку с путём.
    def simplifyPath(self, path: str) -> str:

        # Создаём пустой список, в котором будут храниться
        # компоненты каноничного пути.
        stack = []

        # Метод split('/') разбивает строку path по символу /.
        # Например, для path = "/a/./b/../../c/" мы получим список:
        # ['', 'a', '.', 'b', '..', '..', 'c', ''].
        # Пустые строки появляются из-за начального и возможных конечных слэшей.
        # Цикл перебирает каждую такую часть.
        for part in path.split('/'):

            # Условие проверяет 2 ситуации:
            # part == '' - пустая строка, возникающая при повторяющихся слэшах или
            # начальном/конечном слэше. Она влияет на путь, поэтому игнорится.
            # part == '.' - текущая директория. В каноничном пути она также опускается
            # (никакого эффекта).
            # Если условие истинно, выполняется continue - переход к следующей итерации
            # цикла (обработка следующей части).
            if part == '' or part == '.':
                continue

            # Если текущая часть равна ".." (переход к родительской директории),
            # то при наличии элементов в стеке верхний удаляется. Это соответствует
            # переходу на уровень выше. Если стек пуст (например, в начале абсолютного
            # пути /..), то ничего не делаем (нельзя подняться выше корня).
            elif part == '..':
                if stack:
                    stack.pop()

            # Во всех остальных случаях part считается именем корректной директории
            # или файла (не пустая строка, не . и не ..). Такая часть добавляется в стек.
            else:
                stack.append(part)

        # После обработки всех частей формируется итоговый путь:
        # '/'.join(stack) объединяет элементы стека в одну строку, вставляя между ними символ /.
        # Например, если стек ['a', 'b'], то результат "a/b".
        # '/' + ... добавляет ведущий слэш, т.к. путь абсолютный (начинается с корня).
        # Если стек пуст (например, исходный путь "/" или "/.."), то '/'.join([]) даст пустую
        # строку, и добавление ведущего слэша вернёт "/" - корневой каталог.

        # Таким образом, метод возвращает упрощённый каноничный абсолютный путь.
        return '/' + '/'.join(stack)

# Идея скрипта в том, что нужно принять путь и вернуть его упрощённую версию.
# * Разбиваем строку по '/'.
# * Игнорируем пустые строки и '.'
# * При '..' удаляем последний элемент из стека, если он есть,
# иначе добавляем директорию в стек.
# * Собираем результат.

# ____________________ Примеры. ____________________

# Точка входа в программу.
if __name__ == '__main__':

    # Создаём экземпляр класса.
    sol = Solution()

    # Массив с примерами.
    examples = [
        '/home/',
        '/home//foo/',
        '/home/user/Documents/../Pictures',
        '/../',
        '/.../a/../b/c/../d/./'
    ]

    # Проходимся по всем примерам и выводим их в консоль.
    for ex in examples:
        print(f'Input: {ex}')
        print(f'Output: {sol.simplifyPath(ex)}')
        print() # Автоматом накидывает пустые строки под каждый вывод.