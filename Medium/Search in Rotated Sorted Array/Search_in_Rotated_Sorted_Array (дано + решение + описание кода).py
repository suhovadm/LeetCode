# Search in Rotated Sorted Array

# Имеется целочисленный массив nums, отсортированный по возрастанию
# (все значения уникальны).

# Перед тем как попасть в вашу функцию, массив nums возможно был циклически
# сдвинут влево на неизвестное число позиций k (1 <= k < nums.length), так
# что результирующий массив имеет вид:
# [nums[k], nums[k+1], ..., nums[n-1], nums[0], nums[1], ..., nums[k-1]] (индексация с 0).
# Например, массив [0,1,2,4,5,6,7] после сдвига влево на 3 позиции превращается в [4,5,6,7,0,1,2].

# Дан массив nums после возможного сдвига и целое число target. Верните индекс элемента
# target, если он присутсвует в массиве, или -1, если его нет.

# Вы должны написать алгоритм со временем выполнения O(log n).

# Пример 1:
# Входные данные: nums = [4,5,6,7,0,1,2], target = 0
# Выходные данные: 4

# Пример 2:
# Входные данные: nums = [4,5,6,7,0,1,2], target = 3
# Выходные данные: -1

# Пример 3:
# Входные данные: nums = [1], target = 0
# Выходные данные: -1

# Ограничения:
# 1 <= nums.length <= 5000
# -10^4 <= nums[i] <= 10^4
# Все значения в nums уникальны.
# Массив nums изначально отсортирован по возрастанию, но, возможно, был циклически сдвинут.
# -10^4 <= target <= 10^4

# Импортируем тип List из модуля typing для аннотации типов.
# Это позволяет указать, что функция принимает список целых чисел.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод search, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса.
    # nums: List[int] - входной массив целых чисел (возможно циклически сдвинутый).
    # target: int - искомое число.
    # -> int - метод возвращает целое число (индекс элемента или -1)
    def search(self, nums: List[int], target: int) -> int:

        # Заводим 2 указателя для бинарного поиска:
        # left = 0 - указывает на начало рассматриваемого диапазона.
        # right = len(nums) - 1 - указывает на конец рассматриваемого диапазона (посл. индекс массива).
        left, right = 0, len(nums) - 1

        # Запускаем цикл бинарного поиска. left <= right означает, что поиск продолжается, пока
        # диапазон не станет пустым (когда указатели пересекутся).
        while left <= right:
            # Вычисляем индекс среднего элемента текущего диапазона. Используем целочисленное деление,
            # чтобы получить целый индекс.
            mid = (left + right) // 2

            # Проверяем, равен ли средний элемент целевому значению.
            if nums[mid] == target:
            # Если нашли целевой элемент, немедленно возвращаем его индекс.
            # Это базовый случай бинарного поиска.
                return mid

            # Ключевое условие: определяет, какая часть массива отсортирована.
            # Если nums[left] <= nums[mid], значит левая половина (от left до mid)
            # отсортирована по возрастанию.
            # В циклически сдвинутом массиве всегда одна половина отсортирована,
            # а другая содержит точку разрыва.
            if nums[left] <= nums[mid]:

            # Проверяем, находится ли target в отсортированной левой половине:
            # target должен быть больше или равен крайнему левому элементу.
            # И строго меньше среднего элемента (так как мы уже проверили, что nums[mid] != target).
                if nums[left] <= target < nums[mid]:

                    # Если target в левой половине, сдвигаем правую границу поиска влево
                    # (ищем в диапазоне [left, mid-1]).
                    right = mid - 1

                # Если target не в левой половине, значит он либо в правой половине, либо
                # его нет в массиве.
                # Сдвигаем левую границу вправо (ищем в диапазоне [mid+1, right]).
                else:
                    left = mid + 1

            # Этот блок выполняется, если nums[left] > nums[mid]. Это означает, что левая
            # половина не отсортирована (содержит точку разрыва), следовательно, правая половина
            # (от mid до right) отсортирована.
            else:
                # Проверяем, находится ли target в отсортированной правой половине:
                # target должен быть строго больше среднего элемента.
                # И меньше или равен крайнему правому элементу.
                if nums[mid] < target <= nums[right]:
                    # Если target в правой половине, сдвигаем левую границу поиска вправо
                    # (ищем в диапазоне [mid+1, right]).
                    left = mid + 1

                # Если target не в правой половине, значит он либо в левой половине, либо его нет.
                # Сдвигаем правую границу влево (ищем в диапазоне [left, mid-1]).
                else:
                    right = mid - 1

        # Если цикл завершился (условие left <= right стало ложным) и мы не нашли целевой элемент,
        # возвращаем -1.
        return -1