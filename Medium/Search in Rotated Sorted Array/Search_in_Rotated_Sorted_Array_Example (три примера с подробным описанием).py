# 4. Проверяем, находится ли target в левой половине:
#   4 <= 3 < 7 -> false, значит ищем в правой половине.
# 5. left = mid + 1 = 4. Теперь [0,1,2].
# 6. mid = (4+6) // 2 = 5, nums[5] = 1.
# 7. Левая половина [0,1] отсортирована.
#   Проверяем: 0 <= 3 < 1 -> false, ищем в правой половине -> left = mid + 1 = 6.
# 8. mid = (6+6) // 2 = 6, nums[6] = 2.
#   Проверяем левую половину [2]: 2 <= 3 < 2 -> false.
#   Сдвигаем правую границу: right = mid - 1 = 5.
# 9. Теперь left = 6, right = 5 -> цикл заканчивается.
#   Target не найден.
#   А раз target не найден, то вывод: -1 - числа 3 НЕТ В МАССИВЕ!

