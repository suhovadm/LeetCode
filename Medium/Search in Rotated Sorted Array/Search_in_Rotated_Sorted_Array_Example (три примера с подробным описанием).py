# Создаём экземпляр класса.
solution = Solution()

# Пример 1.
nums1 = [4,5,6,7,0,1,2]
target1 = 0
print('Пример 1:', solution.search(nums1, target1)) # Ожидаемый вывод: 4

# 1. Начальные указатели: left = 0, right = 6 (индексы массива).
# 2. Вычисляем mid = (0 + 6) // 2 = 3.
# nums[3] = 7.
# Это не наш target( 0 ).
# 3. Проверяем, какая половина отсортирована:
#   nums[left] = 4 ≤ nums[mid] = 7 -> левая половина [4,5,6,7] отсортирована.
# 4. Проверяем, находится ли target в левой половине:
#   4 <= 0 < 7 -> false.
#   Значит target может быть только в правой половине.
# 5. Сдвигаем левую границу: left = mid + 1 = 4.
# 6. Теперь left = 4, right = 6. Вычисляем mid = (4+6) // 2 = 5.
# nums[5] = 1 -> не равно target ( 0 ).
# 7. Проверяем отсортированную половину:
#   nums[left] = 0 <= nums[mid] = 1 -> левая половина [0,1] отсортирована.
# 8. Проверяем, находится ли target в этой левой половине:
#   0 <= 0 < 1 -> true.
#   Сдвигаем правую границу: right = mid - 1 = 4.
# 9. Теперь left = 4, right = 4. Вычисляем mid = (4+4) // 2 = 4.
#   nums[4] = 0 -> это и есть наш target! [V]
# ВОЗВРАЩАЕМ 4 !!!
# ВЫВОД: 4 - ИНДЕКС ЧИСЛА 0 В МАССИВЕ !!!

