# Maximum Subarray

# Да целочисленный массив nums, найди подмассив с наибольшей суммой и верни эту сумму.

# Пример 1:
# Входные данные: nums = [-2,1,-3,4,-1,2-1,-5,4]
# Результат: 6
# Пояснение: подмассив [4,-1,2,1] имеет наибольшую сумму 6.

# Пример 2:
# Входные данные: nums = [1]
# Результат: 1
# Пояснение: подмассив [1] имеет наибольшую сумму 1.

# Пример 3:
# Входные данные: nums = [5,4,-1,7,8]
# Пояснение: подмассив [5,4,-1,7,8] имеет наибольшую сумму 23.

# Ограничения:
# 1 <= nums.length <= 105
# -104 <= nums[i] <= 104

# Дополнительное задание: если вы нашли решение со сложностью O(n), попробуйте реализовать
# другое решение, используя подход "разделяй и властвуй", который является более тонким.

# Импортируем тип List из модуля typing для аннотации типов.
# Это позволяет указать, что функция принимает список.
from typing import List

# Заводим класс Solution, как того требует Литкод.
class Solution:
    # Определяем метод maxSubArray, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса.
    # nums: List[int] - параметр, который ожидает список целых чисел.
    # -> int - аннотация, указывающая, что метод возвращает целое число.
    def maxSubArray(self, nums: List[int]) -> int:

    # Переменная для хранения максимальной суммы подмассива,
    # заканчивающегося на текущей позиции.
    # Заводится первым элементом массива, так как на позиции 0
    # подмассив состоит только из этого элемента.
        current_sum = nums[0]
    # Переменная для хранения глобального максимума среди всех
    # просмотренных подмассивов.
    # Тоже начинается с первого элемента, так как мы пока ничего другого не видели.
        max_sum = nums[0]

    # Запускаем цикл.
    # Перебираем индексы от 1 до последнего (начиная со второго элемента).
    # Почему с 1? Потому что первый элемент (индекс 0) мы уже использовали для инициализации.
        for i in range(1, len(nums)):
    # Ключевой момент во всём алгоритме! Здесь принимается ряд решений:
    # nums[i] - начать новый подмассив с текущего элемента,
    # current_sum + nums[i] - продолжить существующий подмассив, добавив текущий элемент,
    # max() - выбирает больший из этих двух вариантов.
    # Логика: если предыдущая сумма отрицательная, выгоднее начать новый подмассив с текущего
    # элемента. Если положительная - выгоднее продолжить.
            current_sum = max(nums[i], current_sum + nums[i])

    # Сравниваем текущий глобальный максимум с новой суммой.
    # max() - сохраняем больший результат.
    # Таким образом, max_sum всегда хранит самую большую сумму, встреченную за всё время.
            max_sum = max(max_sum, current_sum)

    # После завершения цикла возвращаем максимальную найденную сумму.
        return max_sum

# Пример без вывода в консоль:
#i      nums[i]     current_sum(решение)        max_sum
# 0	    -2	        -2 (начальное)	            -2
# 1	    1	        max(1, -2+1=-1) = 1	        max(-2,1)=1
# 2	    -3	        max(-3, 1+(-3)=-2) = -2	    max(1,-2)=1
# 3	    4	        max(4, -2+4=2) = 4	        max(1,4)=4
# 4	    -1	        max(-1, 4+(-1)=3) = 3	    max(4,3)=4
# 5	    2	        max(2, 3+2=5) = 5	        max(4,5)=5
# 6	    1	        max(1, 5+1=6) = 6	        max(5,6)=6
# 7	    -5	        max(-5, 6+(-5)=1) = 1	    max(6,1)=6
# 8	    4	        max(4, 1+4=5) = 5	        max(6,5)=6