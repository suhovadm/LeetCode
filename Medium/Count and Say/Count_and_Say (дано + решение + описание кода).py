# Count and Say

# Последовательность "смотри-и-говори" - это последовательность строк и цифр, определяемая
# рекурсивной формулой:

# countAndSay(1) = "1"
# countAndSay(n) - это результат кодирования длин серий (RLE) для строки countAndSay(n - 1).

# Кодирование длин серии (RLE) - это метод сжатия строки, который работает путём замены
# последовательных одинаковых символов (повторяющихся 2 или более раз) на объединение самого
# символа и числа, обозначающего количество этих символов (длину серии). Например, чтобы сжать
# строку "3322251", мы заменяем "33" на "23", заменяем "222" на "32", заменяем "5" на "15" и
# заменяем "1" на "11". Таким образом, сжатая строка становится "23321511".

# Дано положительное целое число n, верните n-й элемент последовательности "смотри-и-говори".

# Пример 1:
# Вход: n = 4
# Выход: "1211"
# Пояснение:
# countAndSay(1) = "1"
# countAndSay(2) = RLE от "1" = "11"
# countAndSay(3) = RLE от "11" = "21"
# countAndSay(4) = RLE от "21" = "1211"

# Пример 2.
# Вход: n = 1
# Выход: "1"
# Пояснение:
# Это базовый случай.

# Ограничения:
# 1 <= n <= 30

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод countAndSay, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса (обязательный первый аргумент для методов в Python).
    # n: int - номер элемента в последовательности (целое число).
    # -> str - функция возвращает строку (str).
    def countAndSay(self, n: int) -> str:

        # Создаём переменную result и присваиваем ей начальное значение "1".
        # Мы будем использовать эту переменную для хранения текущего элемента
        # последовательности на каждой итерации.
        result = "1"

        # Обработка граничного случая.
        # if n == 1 - проверяем условие: равен ли переданный аргумент n еденице.
        if n == 1:
            # Если условие верно, функция немедленно завершает свою работу и возвращает "1".
            return result

        # Запускаем цикл, который будет выполняться, если n > 1.
        # range(2, n + 1) - создаём последовательность чисел от 2 до n включительно.
        # Например, если n = 4, то i будет последовательно принимать значения 2, 3, 4.
        # Переменная i здесь просто счётчик шагов. На каждом шаге цикла мы будем вычислять
        # countAndSay(i) на основе предыдущего результата.
        for i in range(2, n + 1):

            # current = "" - создаём пустую строку current (текущая). В ней мы будем собирать
            # новый элемент последовательности для текущего шага (i).
            current = ""
            # count = 1 - создаём переменную count (счётчик) со значением 1.
            # Эта переменная будет считать, сколько раз подряд встретилась одна и та же цифра
            # в предыдущей строке result. Мы начинаем с 1, т.к. встретив цифру, мы уже имеем
            # как минимум одно её вхождение.
            count = 1

            # Внутренний цикл, который проходит по предыдущей строке result.
            # range(1, len(result)) означает, что мы начинаем с индекса 1 (второй символ)
            # и идём до последнего индекса строки. Индекс j-1 будет указывать на предыдущий символ.
            for j in range(1, len(result)):

                # Сравниваем текущий символ (result[j]) с предыдущим символом (result[j-1]).
                if result[j] == result[j - 1]:
                    # Если символы равны (т.е. серия одинаковых цифр продолжается), мы
                    # просто увеличиваем наш счётчик на 1.
                    count += 1

                # Если символ НЕ равен предыдущему. Это означает, что серия из одинаковых
                # предыдущих символов закончилась.
                else:
                    # Это ключевое действие Run-Length Encoding (RLE).
                    # str(count) - мы берём значение счётчика (сколько раз встретился символ)
                    # и преобразуем его в строку.
                    # result[j - 1] - мы берём сам символ, серия которого только что закончилась.
                    current += str(count) + result[j - 1]
                    # Сбрасываем счётчик до 1, чтобы начать подсчёт уже для новой серии символов
                    # (той, которую представляет текущий символ result[j]).
                    count = 1

            # Эта строка выполняется после завершения внутреннего цикла for j.
            # Внутренний цикл обработал все сравнения символов, но не добавил
            # информацию о самой последней серии символов в строке. Почему?
            # Цикл добавляет серию в current только в тот момент, когда встречает
            # смену символа (блок else). Для последней группы символов смены символа
            # после неё не происходит, поэтому она не была бы добавлена.
            # result[-1] - это удобный способ получить последний символ строки result.
            # Мы добавляем финальную серию: str(count)  (количество символов в последней
            # группе) + result[-1] (сам последний символ).
            current += str(count) + result[-1]
            # Обновляем переменную result. Теперь result хранит строку, которую мы
            # только что сформировали для текущего шага i. На следующей итерации
            # внешнего цикла (для следующего i) мы будем применять RLE уже к этой новой строке.
            result = current

        # После того как внешний цикл закончил работу (т.е. мы прошли все шаги от 2 до n),
        # перменная result содержит n-й элемент последовательности.
        # Функция возвращает это значение.
        return result