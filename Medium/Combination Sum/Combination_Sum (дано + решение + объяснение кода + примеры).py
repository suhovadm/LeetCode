# Combination Sum

# Дан массив различных целых чисел candidates и целевое целое число target.
# Верните список всех уникальных комбинаций элементов из candidates, сумма
# которых равна target. Комбинации можно возвращать в любом порядке.

# Одно и то же число из candidates может выбираться неограниченное количество раз.
# Две комбинации считаются уникальными, если частота (количество вхождений) хотя
# бы одного из выбранных чисел в них различается.

# Тестовые случаи сгенерированны таким образом, что количество уникальных комбинаций,
# дающих в сумме target, для заданных входных данных меньше 150.

# Пример 1:
# Входные данные: candidates = [2,3,6,7], target = 7
# Выходные данные: [[2,2,3],[7]]
# Пояснение:
# 2 и 3 являются числами-кандидатами, и 2 + 2 + 3 = 7. Обратите внимание, что 2 можно
# использовать многократно.
# 7 является числом-кандидатом, и 7 = 7.
# Это единственно возможные две комбинации.

# Пример 2:
# Входные данные: candidates = [2,3,5], target = 8
# Выходные данные: [[2,2,2,2],[2,3,3],[3,5]]

# Пример 3:
# Входные данные: candidates = [2], target =  1
# Выходные данные: []

# Ограничения:
# 1 <= candidates.length <= 30 (длина массива candidates от 1 до 30)
# 2 <= candidates[i] <= 40 (значения элементов candidates от 2 до 40)
# Все элементы в candidates уникальны (различны).
# 1 <= target <= 40 (целевое число от 1 до 40).

# Импортируем List из модуля typing для аннотации типов.
# Это позволяет узнать, что функция принимает и возвращает списки.
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод combinationSum, который принимает в себя ряд параметров.
    # candidates: список целых чисел (кандидаты для комбинаций).
    # target: целевая сумма.
    # -> List[List[int]] - возвращаем список списков целых чисел (все уникальные комбо).
    def combinationSum(self, candidates: List[int], target: int) -> List[List[int]]:

        # Создаём пустой список result, который будет хранить все найденные комбо.
        result = []

        # Внутренний метод (функция) backtrack.
        # Определяем рекурсивную функцию с 3 параметрами:
        # remaining_target: оставшаяся сумма, которую нужно набрать.
        # start_index: индекс в массиве candidates, с которого начинаем перебор (важно для избежания дублей).
        # current_combination: текущая собираемая комбинация чисел.
        def backtrack(remaining_target: int, start_index: int, current_combination: List[int]) -> None:

        # Базовый случай - нашли комбинацию.
        # Если remaining_target стал равен 0, значит мы набрали нужную сумму!
        # current_combination.copy() создаёт копию текущего списка. Это нужно, потому что
        # без копирования мы бы добавили ссылку на список, который потом будет изменяться.
        # Добавляем эту копию в result.
            if remaining_target == 0:
                result.append(current_combination.copy())
                return

        # Отсечение неподходящих веток.
        # Если remaining_target стал отрицательным, значит сумма превысила целевую.
        # Дальнейшее добавление чисел только ухудшит ситуацию.
        # Просто возвращаемся (отсекаем эту ветку).
            if remaining_target < 0:
                return

        # Основной цикл перебора.
        # Перебираем всех кандидатов, начиная с start_index.
        # Почему с start_index, а не с 0? Это нужно для избежания дублей!
        # Если бы мы начинали с 0 каждый раз, то получили бы и [2,2,3], и [2,3,2], и [3,2,2] -
        # а это одна и та же комбинация.
        # Начиная с текущего индекса, мы гарантируем, что комбинации будут в НЕубывающем порядке.
            for i in range(start_index, len(candidates)):
                candidate = candidates[i]

        # Добавление кандидата и рекурсивный вызов.
        # Добавляем текущего кандидата в комбо.
        # Вызываем backtrack рекурсивно с:
        # - уменьшенной целью: remaining_target - candidate,
        # - тем же индексом i (потому что число можно использовать повторно),
        # - той же комбо (которая теперь включает добавленное число).
                current_combination.append(candidate)
                backtrack(remaining_target - candidate, i, current_combination)

        # Возврат (backtrack).
        # После возврата из рекурсии удаляем последний добавленный элемент.
        # Здесь мы возвращаем комбинацию в исходное состояние для следующей итерации.
        # Таким образом, current_combination всегда содержит путь от корня до текущего узла.
                current_combination.pop()

        # Запускаем рекурсию.
        # Начинаем рекурсию с полной целевой суммой.
        # Начинаем с индекса 0.
        # Начинаем с пустой комбинации.
        backtrack(target, 0, [])

        # Возвращаем собранный результ.
        return result

# Примеры работы:
# candidates = [2,3,6,7], target = 7 --> [[2,2,3], [7]]
# candidates = [2,3,5], target = 8    --> [[2,2,2,2], [2,3,3], [3,5]]
# candidates = [2], target = 1        --> []
# В этой задаче любое число из candidates можно использовать
# неограниченное количество раз, чтобы набрать нужную сумму в target.

# Как это работает?
# 1. Рекурсивно строим комбинации, начиная с пустого списка.
# 2. На каждом шаге добавляем число и уменьшаем оставшуюся сумму.
# 3. Если сумма стала 0 - сохраняем комбинацию.
# 4. Если сумма стала отрицательной - отсекаем ветку.
# 5. start_index гарантирует уникальность комбинаций (неубывающий порядок).