# Дана входная строка s и шаблон p. Реализуйте проверку соответствия регулярному выражению
# с поддержкой символов '.' и '*', где:

# '.' соответствует любому одному слову.
# '*' соответствует нулю или более вхождений предыдущего элемента.

# Соответствие должно покрывать всю входную строку (не частично).

# Пример 1:
# Ввод: s = "aa", p = "a"
# Объяснение: "a" не соответствует всей строке "aa".

# Пример 2:
# Ввод: s = "aa", p = "a*"
# Вывод: true
# Объяснение: '*' означает ноль или более вхождений предыдущего элемента 'a'.
# Следовательно, повторив 'a' один раз, получаем "aa".

# Пример 3:
# Ввод: s = "ab", p = ".*"
# Вывод: true
# Объяснение: ".*" означает "ноль или более (*) любых символов (.)".

# Ограничения:
# 1 <= s.length <= 20
# 2 <= p.length <= 20

# s содержит только строчные английские буквы.
# p содержит только строчные английские буквы, '.' и '*'.
# Гарантируется, что для каждого появления символа '*' перед ним будет находиться допустимый
# символ для сопоставления.

# Импортируем встроенный модуль re (regular expressions) - для работы с регулярными выражениями.
import re

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод isMatch, который принимает в себя ряд параметров:
    # s (тип str) - входная строка для проверки.
    # p (тип str) - шаблон для сопоставления.
    # -> bool: возвращает значение типа True, или False.
    def isMatch(self, s: str, p: str) -> bool:

        # re.fullmatch(p, s) - проверяет, соответствует ли вся строка s шаблону p.
        # Возвращает объект Match (если соответствует) или None (если не соответствует).
        # Если re.fullmatch вернул объект Match - bool() вернёт True.
        # Если re.fullmatch вернул None - bool() вернёт False.
        return bool(re.fullmatch(p, s))

# Примеры использования:
solution = Solution() # Создаём экземпляр класса.

print(solution.isMatch("aa", "a")) # False. Почему? Потому что "a" не равно "aa".
print(solution.isMatch("aa", "a*")) # True. Почему? Потому что "a*" означает 0 или более символов "a".
print(solution.isMatch("ab", ".*")) # True. Почему? Потому что ".*" означает 0 или более любых символов.

# re.fullmatch(p, s) - проверяет, что вся строка s полностью соответствует шаблону p.
# символы . и * в Python regex работают так же, как описано в задаче.
# bool() преобразует результаты в True / False.

