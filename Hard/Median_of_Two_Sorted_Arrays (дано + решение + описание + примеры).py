# Даны два отсортированных массива nums1 и nums2 размера m и n соответственно.
# Найдите медиану двух отсортированных массивов.

# Что такое медиана? Среднее значение в отсортированном наборе чисел.

# Общая сложность алгоритма по времени должна быть O(log (m + n)).

# Пример 1:
# Ввод: nums1 = [1,3], nums2 = [2]
# Вывод: 2.00000
# Объяснение: объединённый массив = [1,2,3], медиана равна 2.

# Пример 2:
# Ввод: nums1 = [1,2], nums2 = [3,4]
# Вывод: 2.50000
# Объяснение: объединённый массив = [1,2,3,4], медиана равна (2 + 3) / 2 = 2.5

# Импортируем List для аннотации списков.
from typing import List

# Заводим класс Solution.
class Solution:
    # Заводим метод, который принимает два списка целых чисел и возвращает медиану как дробное число.
    def findMedianSortedArrays(self, nums1: List[int], nums2: List[int]) -> float:

        # Объединяем два массива в один.
        merged = nums1 + nums2

        # Сортируем объединённый массив по возрастанию.
        merged.sort()

        # Получаем длину объединённого массива.
        n = len(merged)

        # Если длина массива нечётная (остаток от деления на 2 равен 1)
        if n % 2 == 1:
            # Возврат среднего элемента при нечётной длине (n // 2 - целочисленное деление)
            return float(merged[n // 2])

        # Если длина массива чётная.
        else:
            # Первый центральный элемент.
            mid1 = merged[n // 2 - 1]
            # Второй центральный элемент.
            mid2 = merged[n // 2]
            # Возвращаем среднее арифметическое двух центральных элементов.
            return (mid1 + mid2) / 2.0

# Тестирование и запуск.
if __name__ == "__main__":

    # Создаём экземпляр класса, вызываем конструктор.
    solution = Solution()

    # Пример 1.
    nums1 = [1, 3]
    nums2 = [2]
    print(f"Пример 1: {solution.findMedianSortedArrays(nums1, nums2)}") # 2.0

    # Пример 2.
    nums1 = [1, 2]
    nums2 = [3, 4]
    print(f"Пример 2: {solution.findMedianSortedArrays(nums1, nums2)}") # 2.5

    # Пример 3.
    nums1 = [0, 0]
    nums2 = [0, 0]
    print(f"Пример 3: {solution.findMedianSortedArrays(nums1, nums2)}") # 0.0

    # Пример 4.
    nums1 = []
    nums2 = [1]
    print(f"Пример 4: {solution.findMedianSortedArrays(nums1, nums2)}") # 1.0

# Это решение работает следующим образом:
# 1. Объединение массивов: сначала мы просто объединяем два массива с помощью +.
# 2. Сортировка: используем встроенную функцию sort() для сортировки объединённого массива.
# 3. Нахождение медианы:
# - если длина массива нечётная, медиана - это центральный элемент.
# - если длина массива чётная, медиана - это среднее арифметическое двух центральных элементов.

# Для достижения требуемой сложности O(log(m + n)) нужно использовать более сложный алгоритм
# с двоичным поиском. Это решение имеет сложность O((m + n)log(m + n)) из-за сортировки, что не
# соответствует требованию O(log(m + n)). Но оно простое, понятное и правильно работает для
# понимания задачи.
