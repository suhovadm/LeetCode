# Permutation Sequence

# Множество [1, 2, 3, ..., n] содержит всего n! уникальных перестановок.

# Если перечислить и пронумеровать все перестановки по порядку, то для n = 3 получится
# следующая последовательность:

# "123"
# "132"
# "213"
# "231"
# "312"
# "321"

# Даны числа n и k, верните k-ю перестановку из этой последовательности.

# Пример 1:
# Вход: n = 3, k = 3
# Выход: "213"

# Пример 2:
# Вход: n = 4, k = 9
# Выход: "2314"

# Пример 3:
# Вход: n = 3, k = 1
# Выход: "123"

# Ограничения:
# 1 <= n <= 9
# 1 <= k <= n!

# Импортируем функцию factorial из модуля math.
# Она вычисляет факториал числа (например, factorial(3) = 3! = 6).
from math import factorial

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод getPermutation, который принимает в себя ряд параметров.
    # n и k - принимает два целых числа.
    # -> str - возвращает строку (str).
    def getPermutation(self, n: int, k: int) -> str:

    # Создаём список nums со строками от "1" до "n".
    # range(1, n + 1) даёт числа от 1 до n включительно.
    # Генератор списка преобразует каждое число в строку.
    # Например, при n=3: nums = ['1', '2', '3'].
        nums = [str(i) for i in range(1, n + 1)]

    # Уменьшаем k на 1. Это нужно, потому что в условии задачи нумерация начинается с 1,
    # а в программировании удобнее работать с индексацией с 0.
        k -= 1

    # Создаём пустой список result, куда будем собирать цифры результата.
        result = []

    # Запускаем цикл от n до 1 включительно с шагом -1.
    # i уменьшается от n до 1.
    # Каждая итерация определяет очередную цифру в результирующей строке.
        for i in range(n, 0, -1):

        # Вычисляем факториал от i - 1.
        # Для текущей позиции, количество перестановок с фиксированным префиксом равно (i - 1)!
        # Например, если мы выбираем первую цифру (i=n), то оставшиеся n-1 цифр можно переставить
        # (n-1)! способами.
            f = factorial(i - 1)

        # Определяем индекс текущей цифры в списке nums.
        # Индекс показывает, в каком блоке из f перестановок находится наша перестановка.
            index = k // f

        # Удаляем элемент с позиции index из списка nums и возвращаем его.
        # Добавляем эту цифру в результат.
            result.append(nums.pop(index))

        # Обновляем k для следующей итерации.
        # k %= f это то же самое, что и k = k % f.
        # Остаток от деления показывает, какую по счёту перестановку в выбранном блоке нужно взять.
            k %= f

        # Объединяем все цифры из списка result в одну строку и возвращаем её.
        return ''.join(result)