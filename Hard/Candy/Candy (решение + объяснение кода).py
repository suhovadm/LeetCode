# Импортируем тип List из модуля typing для аннотации типов.
# typing - это модуль Python, который содержит инструменты для указания типов данных.
# List - это класс-подсказка, который говорит: "ожидается список".
from typing import List

# Заводим класс Solution, как того требует LeetCode.
class Solution:
    # Определяем метод candy, который принимает в себя ряд параметров.
    # self - ссылка на экземпляр класса, в нашем случае дефолтный параметр. Ничего не делает.
    # ratings: List[int] - параметр с аннотацией типа: ожидается список целых чисел.
    # -> int - метод возвращает целое число.
    def candy(self, ratings: List[int]) -> int:

    # Получаем длину списка.
    # len(ratings) - возвращает количество элементов в списке.
    # Например, если ratings = [1, 0, 2], то len(ratings) вернёт 3.
        n = len(ratings)

    # Создаём список конфет. Здесь происходит 2 действия:
    # 1. [1] - создаём список с одним элементом - числом 1.
    # 2. * n - оператор умножения для списков повторяет список n раз.
    # Важно: для чисел это безопасно, но для изменяемых объектов (списков, словарей)
    # было бы опасно.
    # Пример: если n = 3, то [1] * 3 = [1, 1, 1]
    # Почему именно 1?
    # По условию задачи: "каждый ребёнок должен получить хотя бы одну конфету".
    # Это база от которой мы отталкиваемся.
        candies = [1] * n

    # 1) Первый проход слева направо. range(1, n) - это range(start, stop).
    # range(start, stop) генерирует числа от start (включительно) до stop (не включительно).
    # Здесь start = 1, stop = n.
    # Если n = 3, то range(1, 3) даст 1, 2
    # Почему с 1? Потому что мы сравниваем с предыдущим элементом.
    # Для индекса 0 нет предыдущего элемента.
        for i in range(1, n):

    # Тело цикла:
    # ratings[i] - оценка текущего ребёнка.
    # ratings[i - 1] - оценка ребёнка слева.
    # > - проверка строгого неравенства (не >=, а именно >).
    # Важно: если оценки равны, условие НЕ выполняется (по условию задачи равные
    # оценки не требуют разного количества конфет).
            if ratings[i] > ratings[i - 1]:

    # Если условие истинно (текущая оценка строго больше левой):
    # candies[i - 1] - сколько конфет получил левый ребёнок.
    # +1 - добавляем одну конфету.
    # candies[i] = ... - присваиваем новое значение текущему ребёнку.

    # Результат первого прохода: гарантируется, что для каждой пары "слева направо"
    # у ребёнка с более высокой оценкой конфет больше, чем у левого соседа.
                candies[i] = candies[i - 1] + 1

    # 2) Второй проход справа налево. range(n - 2, -1, -1)
    # range(start, stop, step) с шагом -1.
    # start = n - 2 - последний индекс, для которого существует правый сосед (индекс n-1 не может
    # быть текущим, т.к. у него нет соседа справа).
    # stop = -1 - идём до -1 не включительно, т.е. последнее значение будет 0.
    # step = -1 - движение в обратном направлении.
    # Пример для n = 5:
    # range(3, -1, -1) даст: 3, 2, 1, 0
    # Почему не начинаем с 4? Потому что для индекса 4 нет правого соседа.
        for i in range(n - 2, -1, -1):

    # Тело цикла.
    # ratings[i] - оценка текущего ребёнка.
    # ratings[i + 1] - оценка ребёнка справа.
    # Проверяем, должна ли текущая оценка влиять на количество конфет относительно правого соседа.
            if ratings[i] > ratings[i + 1]:
    # 1. candies[i + 1] + 1 - сколько конфет нужно дать текущему ребёнку, чтобы он получил больше
    # правого соседа.
    # 2. candies[i] - текущее значение конфет (уже установленное при первом проходе).
    # 3. max(... , ...) - выбираем наибольшее из двух.
    # Результат второго прохода: гарантируется, что для каждой пары "справа налево" у ребёнка
    # с более высокой оценкой конфет больше, чем у правого соседа, при этом сохраняются все
    # условия первого прохода.
                candies[i] = max(candies[i], candies[i + 1] + 1)

    # Возврат результата. sum(candies) - встроенная функция, которая складывает все элементы списка.
    # Почему именно сумма? По условию задачи нужно найти минимальное общее количество конфет.
        return sum(candies)